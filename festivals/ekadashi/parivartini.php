      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Glories of Parivartini/Parsva/Vamana Ekadasi</h3>
      <p style="font-size:var(--font-size-sm); color:var(--text-light); font-weight:600; margin-bottom:0;">
        From the Brahma-vaivarta Purana
      </p>
      
      <p>
        Sri Yudhishthira Maharaja asked of Lord Sri Krishna, "What is the name of that Ekadasi that occurs during the light fortnight (shukla paksha) of the month of Bhadrapada (August-September), who is the worshipable Deity for this Ekadasi, and what merit does one attain in observing it? Kindly reveal all of this to me, my Lord."
      </p>
      
      <p>
        The Supreme Personality of Godhead Lord Sri Krishna replied, "This Ekadasi, Oh Yudhishthira, is called Vamana Ekadasi, and it bestows upon those who observe it both great merit and ultimate liberation from material bondage. Because it removes all one's sinful reactions, it is also called Jayanti Ekadasi. Just hearing of its glories frees one from all past misdeeds. So auspicious is this fast that observing it bestows the same merit as that earned by performing a horse sacrifice."
      </p>
      
      <p>
        "While observing this holy fast, a Vaishnava should lovingly worship the Supreme Lord in His form as Vamanadeva, the dwarf incarnation, whose eyes are like lotus petals. By doing so, he worships all other Deities as well, including Brahma, Vishnu, and Shiva, and at death he goes to the abode of Sri Hari. The reason this Ekadasi is so auspicious is that it celebrates the day when the sleeping Lord Vishnu turns over on His other side; thus it is also known as Parivartini Ekadasi."
      </p>
      
      <p>
        Maharaja Yudhishthira then asked, "Oh Janardana, please clear up a question I have. How is it that the Supreme Lord sleeps and then turns over on His side? Oh Lord, when You are asleep what happens to all the other living entities? Please also tell me how You bound the king of the demons, Bali Daityaraj (Bali Maharaja), as well as how one may please the brahmanas."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Story of Bali Maharaja and Lord Vamanadeva</h3>
      <p>
        Lord Sri Krishna replied, "Oh Yudhishthira, lion among Kings, I will gladly narrate to you an historical event. In the Treta-yuga, there once was a king named Bali. Though born in a family dynasty of demons, he was very devoted to Me. He sang many Vedic hymns and performed fire sacrifices to satisfy Me. He respected the twice-born brahmins and engaged them daily."
      </p>
      
      <p>
        "This great soul had a quarrel with Indra, however, and eventually defeated him in battle. Bali took over his entire celestial kingdom. Therefore, Indra and all the demigods, along with many great sages, approached Me and complained about Bali Maharaja. Bowing their heads, they worshipped Me. Thus, I agreed to appear on their behalf as dwarf Vamanadeva, My fifth incarnation."
      </p>
      
      <p>
        Yudhishthira enquired, "Oh Lord, how was it possible for You to conquer such a powerful demon in the form of a dwarf brahmin?"
      </p>
      
      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "Lord Sri Krishna replied: 'Though a dwarf, I was a brahmana, and I approached Bali to ask for alms. I said: "Oh Daityaraj Bali, please give Me just three steps of land in charity." Bali agreed to grant My request. But as soon as he vowed, My body began to expand into a gigantic transcendental form. I covered the Earth with My feet, Bhuvarloka with My thighs, Swarga with My waist, Maharloka with My stomach, Janaloka with My chest, Tapoloka with My neck, and Satyaloka with My head. I covered the entire material creation.'"
      </blockquote>

      <p>
        "Seeing this pastime, all the demigods and Shesha Naga began to sing Vedic hymns. I took Bali by the hand and said: 'Oh sinless one, I have covered the Earth with one step and the heavens with the second. Now where shall I place My foot to measure the third step?' Upon hearing this, Bali Maharaja bowed down in humility and offered Me his head to place My third step. I placed My foot on his head and sent him to Patalaloka. Seeing him thus humbled, I became very pleased and told Bali that henceforth I would permanently reside in his palace. On Parivartini Ekadasi, Bali installed a Deity form of Me in his residence."
      </p>
      
      <p>
        "Until Haribodhini Ekadasi in Karttika, I continue to sleep in the ocean of milk. One should therefore observe Parivartini Ekadasi carefully. On this day, the devotee should worship Lord Trivikrama (Vamanadeva), because on this day I turn over to sleep on My other side. If possible, one should give a qualified person some yogurt mixed with rice, as well as some silver, and remain awake throughout the night. This will free one of all material conditioning, equivalent to a thousand horse sacrifices."
      </p>

