      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Glories of Varuthini Ekadasi</h3>
      <p style="font-size:var(--font-size-sm); color:var(--text-light); font-weight:600; margin-bottom:0;">
        From the Bhavishya Purana
      </p>
      
      <p>
        Sri Yudhisthira Maharaj said, "Oh Vasudeva, I offer my most humble obeisances unto You. Please now describe to me the Ekadasi of the dark fortnight (krishna paksha) of the month of Vaisakha (April-May), including its specific merits and influence."
      </p>
      
      <p>
        Lord Sri Krishna replied, "Oh King, in this world and the next, the most auspicious and magnanimous Ekadasi is Varuthini Ekadasi, which occurs during the dark fortnight of the month of Vaisakha. Whosoever observes a complete fast on this sacred day has his sins completely removed, obtains continuous happiness, and achieves all good fortune."
      </p>
      
      <p>
        "Fasting on Varuthini Ekadasi makes even an unfortunate woman fortunate. Upon anyone who observes it, this Ekadasi bestows material enjoyment in this life and liberation after the death of this present body. It destroys the sins of all and saves people from the miseries of repeated rebirth."
      </p>
      
      <p>
        "By observing this Ekadasi properly, King Mandhata was liberated. Many other kings also benefited from observing it – kings such as Maharaj Dhundhumara, in the Ikshvaku dynasty, who became free from leprosy resulting from the curse that Lord Shiva had imposed upon him as a punishment."
      </p>
      
      <p>
        "Whatever merit one obtains by performing austerities and penances for ten thousand years is achieved by a person who observes Varuthini Ekadasi. The merit one achieves by donating a great amount of gold during a solar eclipse at Kurukshetra is gained by one who observes this one Ekadasi with love and devotion, and certainly attains his goals in this life and the next. In short, this Ekadasi is pure and very enlivening and the destroyer of all sins."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Hierarchy of Charities</h3>
      <p>
        "Better than giving horses in charity is giving elephants, and better than giving elephants is giving land. But better still than giving land is the giving of sesame seeds, and better than that is giving of gold. Still better than giving gold is giving food grains – for all the forefathers, demigods, and human beings become satisfied by eating grains. Thus there is no better gift of charity than this in the past, present or future."
      </p>
      
      <p>
        "Yet learned scholars have declared that giving away a young maiden in marriage to a worthy person is equal to giving away food grains in charity. Moreover, Lord Sri Krishna, the Supreme Personality of Godhead, has said that giving cows in charity is equal to giving food grains. Still better than all these charities is teaching spiritual knowledge to the ignorant. Yet all the merits one can attain by performing all these acts of charity are attained by one who fasts on the Varuthini Ekadasi."
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "One who lives off the wealth of his daughters suffers a hellish condition until the inundation of the entire universe, Oh Bharata. Therefore one should be especially careful not to use the wealth of his daughter. Any householder who takes his daughter's wealth out of greed, who tries to sell his daughter, or who takes money from the man to whom he has given his daughter in marriage – such a householder becomes a lowly cat in his next life."
      </blockquote>

      <p>
        "Therefore it is said that whoever, as a sacred act of charity, gives away in marriage a maiden decorated with various ornaments, and who also gives a dowry with her, obtains merit that cannot be described even by Chitragupta, the chief secretary of Yamaraja in the heavenly planets. That very same merit, however, can be easily achieved by one who fasts on the Varuthini Ekadasi."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Regulative Principles of the Fast</h3>
      <p>
        The scriptures detail specific practices to be observed on the three days surrounding the fast:
      </p>

      <p><strong>On Dashami (the day before the fast):</strong> Give up eating on bell-metal plates, eating urad-dahl, red-lentils, chick-peas, or kondo (a grain eaten by the poor), eating spinach, eating honey, eating in another person's home, eating more than once, and participating in sex of any kind.</p>
      
      <p><strong>On Ekadasi (the day of the fast):</strong> Give up gambling, sports, sleeping during the daytime, betel nuts and its leaf, brushing one's teeth, spreading rumors, faultfinding, talking to the spiritually fallen, anger, and lying.</p>
      
      <p><strong>On Dvadashi (the day after the fast):</strong> Give up eating on bell-metal plates, eating urad-dahl, red-lentils, or honey, lying, strenuous exercise or labor, eating more than once, any sexual activity, shaving the body, face or head, smearing oils on one's body, and eating in another's home.</p>
      
      <p>
        Lord Sri Krishna continued, "Whoever observes the Varuthini Ekadasi in this way becomes free from all sinful reactions and returns to the eternal, spiritual abode. One who worships Lord Janardana on this Ekadasi by staying awake throughout the entire night also becomes free from all his previous sins and attains to the spiritual abode. Therefore, Oh king, he who is frightened of his accumulated sins and their reactions, and thus of death itself, must observe Varuthini Ekadasi by fasting very strictly. Finally, Oh noble Yudhishthira, he who hears or reads this glorification of the sacred Varuthini Ekadasi obtains the merit earned by donating one thousand cows in charity, and at last he returns home to the Supreme abode of Lord Vishnu in the Vaikunthas."
      </p>

