      <p>
        Varuthini Ekadasi falls in the dark fortnight (Krishna paksha) of the month of Vaisakha. It corresponds to the months of April-May in the Gregorian calendar. On this day, devotees worship and offer prayers to Lord Vamana, the dwarf incarnation of Lord Vishnu. In the literal sense, "Varuthini" means protected, and thus the devotees get protected from various negativities and evils by observing Varuthini Ekadashi.
