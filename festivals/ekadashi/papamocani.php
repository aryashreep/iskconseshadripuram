      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Glories of Papamocani Ekadasi</h3>
      <p style="font-size:var(--font-size-sm); color:var(--text-light); font-weight:600; margin-bottom:0;">
        From the Bhavishya-uttara Purana
      </p>
      
      <p>
        Sri Yudhisthira Maharaja said, "Oh Supreme Lord, I have heard from You the explanation of Amalaki Ekadasi which occurs during the light fortnight of the month of Phalguna (February-March), and now I wish to hear about the Ekadasi that occurs during the dark fortnight of the month of Chaitra (March-April). What is its name, Oh Lord, and what results can one attain by observing it?"
      </p>
      
      <p>
        The Supreme Personality of Godhead, Lord Sri Krishna, replied, "O best of kings, for the benefit of everyone I shall gladly describe to you the glories of this Ekadasi, which is known as Papamochani. The history of this Ekadasi was once narrated to the Emperor Mandhata by Lomasha Rishi."
      </p>
      
      <p>
        "King Mandhata addressed the Rishi, 'Oh great sage, for the benefit of all people, please tell me the name of the Ekadasi that occurs during the dark fortnight of the month of Chaitra, and please explain the process for observing it. Also, please describe the benefits one gains by observing this Ekadasi.'"
      </p>
      
      <p>
        "Lomasha Rishi replied, 'The Ekadasi that occurs during the dark part of the month of Chaitra is named Papamochani Ekadasi. For the faithful devotee it removes the influences of ghosts and demons. Oh lion among men, this Ekadasi also awards the eight perfections of life, fulfills all kinds of desires, purifies one's life of all sinful reactions, and makes a person perfectly virtuous.'"
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Seduction of Sage Medhavi</h3>
      <p>
        "Now please listen to a historical account concerning this Ekadasi and Chitraratha, the chief of the Gandharvas (heavenly musicians). During the spring season, in the company of heavenly dancing girls, Chitraratha once came upon a beautiful forest bursting forth with a great variety of flowers. There he and the girls joined the Gandharvas and many Kinnaras, along with Lord Indra himself, the king of heaven, who was enjoying a visit there. Many sages were also present, performing their austerities and penances."
      </p>
      
      <p>
        "A great sage named Medhavi resided in that forest, and the very attractive dancing girls would always attempt to seduce him. One famous girl in particular, Mañjughosha, contrived many ways to allure the exalted Muni, but out of great respect for the sage and fear of his power, which he had attained after years and years of ascetics, she would not come very close to him. At a spot two miles from the sage, she pitched a tent and began singing very sweetly as she played a tamboura. Cupid himself became excited when he saw and heard her perform so nicely and decided to take revenge by seducing Medhavi."
      </p>
      
      <p>
        "Using the eyebrows of Mañjughosha as a bow, her glances as a bowstring, her eyes as arrows, and her breasts as a target, Cupid approached Medhavi in order to tempt him to break his trance and his vows. Cupid engaged Mañjughosha as his assistant, and when she looked at that powerful and attractive young sage, she also became agitated by lust. Seeing that he was highly intelligent and learned, wearing a clean white brahmana's thread draped across his shoulder, holding a sannyasi's staff, and sitting handsomely in the ashrama of Chyavana Rishi, Mañjughosha came before him."
      </p>
      
      <p>
        "She began to sing seductively, and the small bells of her belt and around her ankles, together with the bangles on her wrists, produced a delightful musical symphony. The sage Medhavi was enchanted. He understood that this beautiful young woman desired union with him, and at that instant Cupid increased his attraction for Mañjughosha by releasing his powerful weapons of taste, touch, sight, smell, and sound."
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "Slowly Mañjughosha approached Medhavi, her bodily movements and sweet glances attracting him. She gracefully put her tamboura down and embraced the sage with her two arms, just as a creeper winds itself around a strong tree. Captivated, Medhavi gave up his meditation and decided to sport with her – and instantly his purity of heart and mind abandoned him. Forgetting even the difference between night and day, he went away with her to sport for a long, long time."
      </blockquote>

      <p>
        "Seeing that the young yogi's sanctity had become seriously eroded, Mañjughosha decided to abandon him and return home. She said, 'O great one, please permit me to return home.'"
      </p>
      
      <p>
        "Medhavi replied, 'But you have only just arrived, O beautiful one. Please stay with me at least until tomorrow.'"
      </p>
      
      <p>
        "Fearful of the sage's yogic power, Mañjughosha stayed with Medhavi for precisely fifty-seven years, nine months, and three days, but to Medhavi all this time seemed like a moment. Again she asked him, 'Please permit me to leave.'"
      </p>
      
      <p>
        "Medhavi replied, 'O dear one, listen to me. Stay with me for one more night, and then you may leave tomorrow morning. Just stay with me until I have performed my morning duties and chanted the sacred Gayatri mantra.'"
      </p>
      
      <p>
        "Mañjughosha was still fearful, but she forced a smile and said, 'How long will it take you to finish your morning hymns and rituals? Please be merciful and think of all the time you have already spent with me.' The sage reflected on the years he had been with Mañjughosha and then said with great astonishment: 'Why, I have spent more than fifty-seven years with you!' His eyes turned red and began to emanate sparks. He now regarded Mañjughosha as death personified and the destroyer of his spiritual life."
      </p>
      
      <p>
        "'You rascal woman! You have turned all the hard-earned results of my austerities to ashes!' Trembling with anger, he cursed Mañjughosha: 'Oh sinful one, Oh hard-hearted, degraded one! You know only sin! May all terrible fortune be yours! Oh rascal woman, I curse you to become an evil hobgoblin – <em>pishacha</em>!'"
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Redemption and Atonement</h3>
      <p>
        "Cursed by the sage Medhavi, the beautiful Mañjughosha humbly beseeched him, 'Oh best of the brahmanas, please be merciful to me and revoke your curse! Oh great one, it is said that association with pure devotees gives immediate results but their curses take effect only after seven days. I have been with you for fifty-seven years, Oh master, so please be kind to me!'"
      </p>
      
      <p>
        "Medhavi Muni replied, 'Oh gentle lady what can I possibly do? You have destroyed all my austerities. But even though you have done this sinful deed, I shall tell you a way you can be released from my wrath. In the dark fortnight of the month of Chaitra there is an all-auspicious Ekadasi that removes all one's sins. Its name is Papamochani, and whoever fasts on this sacred day becomes completely freed from having to take birth in any kind of devilish form.'"
      </p>
      
      <p>
        "With these words, the sage left at once for his father's ashrama. Seeing him enter the hermitage, Chyavana Muni said, 'Oh son, by acting unlawfully you have squandered the wealth of your penances and austerities.'"
      </p>
      
      <p>
        "Medhavi replied, 'Oh Father, kindly reveal what atonement I must perform to remove the obnoxious sin I have incurred by privately associating with the dancing girl Mañjughosha.'"
      </p>
      
      <p>
        "Chyavana Muni answered, 'Dear son, you must fast on Papamochani Ekadasi, which occurs during the dark fortnight of the month of Chaitra. It eradicates all sins, no matter how grievous they may be.'"
      </p>
      
      <p>
        "Medhavi followed his father's advice and fasted on Papamochani Ekadasi. Thus all his sins were destroyed and he again became filled with excellent merit. Similarly, Mañjughosha observed the same fast and became free of the hobgoblin curse. Ascending once again to the heavenly spheres, she too returned to her former position."
      </p>
      
      <p>
        Lomasha Rishi concluded, "Thus, Oh king, the great benefit of fasting on Papamochani Ekadasi is that whoever does so with faith and devotion will have all his sins completely destroyed."
      </p>
      
      <p>
        Lord Sri Krishna concluded, "Oh King Yudhishthira, whoever reads or hears about Papamochani Ekadasi obtains the very same merit he would get if he donated a thousand cows in charity, and he also nullifies the sinful reactions he may have incurred by killing a brahmana, killing an embryo through abortion, drinking liquor, or having sex with his guru's wife."
      </p>

