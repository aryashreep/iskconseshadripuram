      <p>
        Sat-tila Ekadasi is also known as Trisprisha Ekadasi. This Ekadasi occurs in the dark fortnight of the month of Magha (January/February). The glories of Sat-tila Ekadasi are narrated in the Bhavisyottara Purana in a conversation between the sage Dalbhya and Pulastya Muni.
      </p>
      
      <p>
        It is an auspicious start for the rest of the year, and we encourage devotees reading this to kindly consider donating on this day.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Glories of Sat-tila Ekadasi</h3>
      <p style="font-size:var(--font-size-sm); color:var(--text-light); font-weight:600; margin-bottom:0;">
        From the Bhavisyottara Purana
      </p>
      
      <p>
        Sri Daalbhya Rishi said to Pulastya Muni, "When the spirit soul comes in contact with the material energy, he immediately begins to perform sinful activities, such as stealing, killing, and illicit sex. He may even perform many other terrible deeds, such as killing a brahmana. Oh purest of personalities, please tell me how these unfortunate souls may escape the punishment of being sent to hellish regions of creation. Kindly inform me how, by giving even a little in charity, one may be easily released from the reactions of his sins."
      </p>
      
      <p>
        Pulastya Muni replied, "Oh fortunate one, you have asked me an important and confidential question, which not even Brahma, Vishnu, Shiva, or Indra has ever asked. Please listen very carefully to my answer."
      </p>
      
      <p>
        "With the arrival of the month of Magha (January - February), one should bathe, carefully control his senses by giving up lust, anger, pride, jealousy, faultfinding, and greed, and meditate on the Supreme Personality of Godhead Lord Sri Krishna. One should then gather up some cow dung before it touches the ground and, after mixing it with sesame seeds and cotton, form 108 balls. This should be done on the day when the constellation of Purva-ashadha nakshatra arrives. Then one should follow the rules of Sri Ekadasi."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Six Pure Uses of Sesame (Tila)</h3>
      <p>
        Pulastya Muni explained that on this day, black sesame seeds are especially suitable for formal worship and fire sacrifices:
      </p>
      
      <ol style="margin-left:var(--space-lg); line-height:1.8; display:flex; flex-direction:column; gap:6px;">
        <li><strong>Bathing</strong> in water mixed with sesame seeds.</li>
        <li><strong>Rubbing</strong> sesame seed paste (ubtan) on the body.</li>
        <li><strong>Offering</strong> sesame seeds into the sacred fire in sacrifice (108 cow dung & sesame balls).</li>
        <li><strong>Eating</strong> sesame seeds.</li>
        <li><strong>Giving</strong> sesame seeds away in charity.</li>
        <li><strong>Accepting</strong> charitable gifts of sesame seeds.</li>
      </ol>
      
      <p>
        These are the six (sat) ways in which sesame seeds (tila) are utilized for spiritual purification on this Ekadasi. Therefore it is called Sat-tila Ekadasi.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Stingy Brahmini and the Lump of Mud</h3>
      <p>
        Lord Sri Krishna personally narrated the following story to Devarishi Narada Muni:
      </p>
      
      <p>
        "Long ago on earth, there lived an old female brahmini who worshipped Me every day with controlled senses. She very faithfully observed many a fast and served Me with full devotion, devoid of any personal motive. Her rigorous fasting made her quite weak and thin. She gave charity to brahmanas and young maidens, and even planned to give away her house. Although this spiritually minded woman gave charitable donations to worthy people, the odd feature of her austerity was that she never gave food to brahmanas or the devas."
      </p>
      
      <p>
        "I began to reflect on this curious omission: 'This fine woman has purified herself by fasting and devotional worship. Therefore she has become eligible to enter My personal abode. So I came down to this planet to examine her, disguising Myself as a follower of Lord Shiva's, complete with a begging pot (kamandalu) in My hand."
      </p>
      
      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "As I approached her, she said to Me, 'Oh respectful one, tell me truthfully why You have come before me.' I replied, 'Oh beautiful one, I have come to get some alms from you' – whereupon she angrily threw a dense lump of mud into My begging pot! I simply turned around and went back to My abode."
      </blockquote>

      <p>
        "At last, this austere lady reached the spiritual world in her self-same body. And because she had offered Me a lump of mud, I transformed that mud into a beautiful home. However, this house was completely empty of any grains, food, or furniture. She approached Me and said, 'I have fasted repeatedly on so many occasions, making my body weak and thin. Yet there is no food or wealth to be seen in my new home, Oh Janardana. Why is this?'"
      </p>
      
      <p>
        "I replied, 'Please return to your house. Sometime later the wives of the devas (demigods) will pay you a visit out of curiosity. Do not open your door until they have described to you the glories and importance of Sat-tila Ekadasi.'"
      </p>
      
      <p>
        "Hearing this, she returned. When the devas' wives arrived and asked to see her, she replied, 'If you want me to open this door, you must describe to me the merit of Sat-tila Ekadasi.' One of the wives nicely explained the sublime nature of this sacred Ekadasi. When the lady opened her door, they saw that she was simply an ordinary human lady."
      </p>
      
      <p>
        "From then on, the lady observed Sat-tila Ekadasi, which awards all material enjoyment and liberation at the same time. She finally received the beautiful furnishings and grains she had expected. Moreover, her body was transformed into a beautiful spiritual form made of sac-cid-ananda (eternality, knowledge, and bliss)."
      </p>
      
      <p>
        Lord Sri Krishna concluded, "Oh Naradaji, a person should not ostentatiously observe Ekadasi out of greed. Selflessly, he should simply donate sesame seeds, clothes, and food according to his capacity. Following this Ekadasi fast by donating, sacrificing, or eating sesame seeds frees one of all past sins, without a doubt."
      </p>

