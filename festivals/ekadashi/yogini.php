      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Glories of Yogini Ekadasi</h3>
      <p style="font-size:var(--font-size-sm); color:var(--text-light); font-weight:600; margin-bottom:0;">
        From the Brahma-vaivarta Purana
      </p>
      
      <p>
        Yudhisthira Maharaj said, "Oh Supreme Lord, I have heard the glories of the Nirjala Ekadasi, which occurs during the light fortnight of the month of Jyeshtha (May – June). Now I wish to hear from You about the suddha Ekadasi that occurs during the dark fortnight of the month of Ashadha (June – July). Kindly describe to me all about it in detail, Oh killer of the Madhu demon (Madhusudana)."
      </p>
      
      <p>
        The Supreme Lord, Sri Krishna, then replied, "Oh king, I shall indeed tell you about the best of all fasting days, the Ekadasi that comes during the dark part of the month of Ashadha. Famous as Yogini Ekadasi, it removes all kinds of sinful reactions and awards supreme liberation."
      </p>
      
      <p>
        "Oh best of kings, this Ekadasi delivers people who are drowning in the vast ocean of material existence and transports them to the shore of the spiritual world. In all the three worlds, it is the chief of all sacred fasting days. I shall now reveal this truth to you by narrating a history recounted in the Puranas."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Story of Hemamali</h3>
      <p>
        "The king of Alakapuri – Kuvera, the treasurer of the devas (demigods) – was a steadfast devotee of lord Shiva. He employed a servant named Hemamali as his personal gardener. Hemamali, a Yaksha like Kuvera, was very lustfully attracted to his gorgeous wife, Swarupavati, who had large, enchanting eyes."
      </p>
      
      <p>
        "Hemamali's daily duty was to visit Manasarovara Lake and bring back flowers for his master, Kuvera, with which he would use them in the puja offerings to lord Shiva. One day, after picking the flowers, Hemamali went to his wife instead of returning directly to his master and fulfilling his duty by bringing the flowers for the puja. Absorbed in loving affairs of a bodily nature with his wife, he forgot to return to the abode of Kuvera."
      </p>
      
      <p>
        "Oh king, while Hemamali was enjoying with his wife, Kuvera had begun the worship of lord Shiva as normal in his palace and soon discovered that there were no flowers ready to be offered in the midday puja. The lack of such an important item (upachara) angered the great Koshad-yaksha (treasurer of the devas) even more, and he asked a Yaksha messenger, 'Why has dirty-hearted Hemamali not come with the daily offering of flowers? Go find out the exact reason and report back to me in person with your findings.' The Yaksha returned and told Kuvera, 'Oh dear lord, Hemamali has become lost in freely enjoying coitus with his wife.'"
      </p>
      
      <p>
        "Kuvera became extremely angry when he heard this and at once summoned lowly Hemamali before him. Knowing that he had been remiss and dawdling in his duty and exposed as meditating on his wife's body, Hemamali approached his master in great fear. The gardener first paid his obeisances and then stood before his lord, whose eyes had become red with anger and whose lips trembled in rage. So enraged, Kuvera cried out to Hemamali, 'Oh you sinful rascal! Oh destroyer of religious principles! You are a walking offense to the devas! I therefore curse you to suffer from white leprosy and to become separated from your beloved wife! Only great suffering is deservedly yours! Oh lowborn fool, leave this place immediately and betake yourself to the lower planets to suffer!'"
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "And so Hemamali fell at once from grace in Alakapuri and became ill with the terrible affliction of white leprosy. He awoke in a dense and fearful forest, where there was nothing to eat or drink. Thus he passed his days in misery, unable to sleep at night due to pain."
      </blockquote>

      <p>
        "He suffered in both winter and summer season, but because he continued to worship lord Shiva himself with faith, his consciousness remained purely fixed and steady. Although implicated by great sin and its attendant reactions, he remembered his past life because of his piety."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Meeting with Sage Markandeya</h3>
      <p>
        "After wandering for some time here and there, over mountains and across plains, Hemamali eventually came upon the vast expanse of the Himalayan mountain ranges. There he had the wonderful good fortune to come in contact with the great saintly soul Markandeya Rishi, the best of ascetics, whose duration of life it is said, extends to seven of the days of Brahma."
      </p>
      
      <p>
        "Markandeya Rishi was seated peacefully at his ashrama, looking as effulgent as a second Brahma. Hemamali, feeling very sinful, stood at a distance from the magnificent sage and offered his humble obeisances and choice prayers. Always interested in the welfare of others, Markandeya Rishi saw the leper and called him near: 'Oh you, what sort of sinful deeds have you done to earn this dreadful affliction?'"
      </p>
      
      <p>
        "Hearing this, Hemamali painfully and ashamed replied, 'Dear sir, I am a Yaksha servant of lord Kuvera, and my name is Hemamali. It was my daily service to pick the flowers from the Manasarovara lake for my master's worship of lord Shiva, but one day I was negligent and was late in returning with the offering because I had become overwhelmed with lusty passion for enjoying bodily pleasures with my wife. When my master discovered why I was late, he cursed me in great anger to be as I am before you. Thus I am now bereft of my home, my wife, and my service. But fortunately I have come upon you, and now I hope to receive from you an auspicious benediction, for I know that devotees such as you are as merciful as the Supreme Lord and always carry the interest of others uppermost in their hearts. That is your nature. Oh best of sages, please help me!'"
      </p>
      
      <p>
        "Softhearted Markandeya Rishi replied, 'Because you have told me the truth, I shall tell you about a fast day that will benefit you greatly. If you fast on the Ekadasi that comes during the dark fortnight of the month of Ashadha, you will surely be freed of this terrible curse.'"
      </p>
      
      <p>
        "Hemamali fell to the ground in complete gratitude and offered him his humble obeisances again and again. But Markandeya Rishi stood there and lifted poor Hemamali up to his feet, filling him with inexpressible happiness."
      </p>
      
      <p>
        "Thus, as the sage had instructed him, Hemamali dutifully observed the Ekadasi fast, and by its influence he again became a handsome Yaksha. Then he returned home, where he lived very happily with his wife."
      </p>
      
      <p>
        Lord Sri Krishna concluded, "So, you can readily see, Oh Yudhishthira, that fasting on Yogini Ekadasi is very powerful and auspicious. Whatever merit one obtains by feeding eighty-eight thousand brahmins is also obtained simply by observing a strict fast on Yogini Ekadasi. For one who fasts on this sacred Ekadasi, she (Ekadasi Devi), destroys heaps of past sinful reactions and makes him most pious. Oh King, thus I have explained to you the purity of Yogini Ekadasi."
      </p>

