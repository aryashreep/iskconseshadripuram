      <p>
        Gita Jayanti is the auspicious day of the advent of Srimad Bhagavad-gita. This is the day on which Lord Krishna imparted the essence of Vedic knowledge to Arjuna over 5,000 years ago and enlightened him about the ultimate goal of life. This day is also called Mokshada Ekadashi, and it generally occurs in the month of December.
      </p>
      
      <p>
        As stated in the Gita-mahatmya, if one properly follows the instructions of Bhagavad-gita, one can be freed from all miseries and anxieties in this life, and one's next life will be spiritual.
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
