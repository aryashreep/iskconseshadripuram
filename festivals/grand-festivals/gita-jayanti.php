      <ul style="margin-left:var(--space-lg); line-height:1.8; display:flex; flex-direction:column; gap:6px;">
        <li><strong>Sensitivity to Reality:</strong> Teaches awareness of both the material and spiritual realities.</li>
        <li><strong>A Willing Dialogue:</strong> The conversation between an eager disciple and a caring teacher.</li>
        <li><strong>Stability in Action:</strong> Impels us to understand our field of activity (Kshetra) and function with stability.</li>
        <li><strong>Harmonizing Opposites:</strong> War on the outside, peace on the inside; fierce action without attachment to outcomes.</li>
        <li><strong>Selfless Service:</strong> Balances self-growth with dedicated, selfless service to the Supreme.</li>
        <li><strong>Respect for Time:</strong> Reminds us to respect eternal time and make every moment thoughtful.</li>
        <li><strong>Divine Vision:</strong> Inspires us to see divinity everywhere and see everything within the divine.</li>
