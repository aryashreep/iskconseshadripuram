        vivasvan manave praha manur iksvakave bravit"<br><br>
        "The Blessed Lord said: I instructed this imperishable science of yoga to the sun-god, Vivasvan, and Vivasvan instructed it to Manu, the father of mankind, and Manu in turn instructed it to Ikshvaku." &mdash; Bhagavad-gita 4.1
      </blockquote>

      <p>
        Gita Jayanti is not simply a historical birthday; it is the day the Bhagavad-gita was spoken for the first time on this planet during this particular yuga cycle. As Lord Krishna explains, this supreme science was received through the chain of disciplic succession, but in course of time the succession was broken, and therefore the science appeared to be lost (BG 4.2). This shows that the message of the Bhagavad-gita is eternally present, and Lord Krishna delivers it in its original form as and when it needs to be revived.
      </p>

