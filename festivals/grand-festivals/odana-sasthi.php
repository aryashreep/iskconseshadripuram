        "No one can understand the glories of the Lord's servants. Only Krishna knows their attachment for Him. Krishna sometimes bewilders His devotee, and then, feeling compassion for him, He destroys his illusion." &mdash; Sri Caitanya Bhagavata, Antya-khanda texts 120-121
      </blockquote>

      <p>
        The Odana-sasthi festival is most definitely a favorite winter festival in Sri Jagannatha Mandir, bringing devotees together to blissfully celebrate the pastime activities of Lord Jagannatha.
      </p>

