      <p>
        As autumn surrenders to the cool breeze of the winter, we are reminded of the approaching winter season and the divine festival of Odana Sasthi. This auspicious occasion reminds us of the Lord's loving reciprocation with His devotees by accepting a starched cloth for winter.
      </p>
      
      <p>
        Odana-sasti is the date on which Lord Jagannatha is given a winter shawl. One year when Lord Caitanya and His associates celebrated this festival in Puri, Pundarika Vidyanidhi, who is Vrsabhanu Maharaja (Srimati Radharani's father in krsna-lila), received some special mercy. His experience is instructive for us all.
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "gadādhara-paṇḍite teṅho punaḥ mantra dila<br>
        oḍana-ṣaṣṭhīra dine yātrā ye dekhila"<br><br>
        "Puṇḍarīka Vidyānidhi initiated Gadādhara Paṇḍita for the second time, and on the day of Oḍana-ṣaṣṭhī Puṇḍarīka Vidyānidhi saw the festival." &mdash; Sri Caitanya Caritamrta Madhya Lila 16.78
      </blockquote>

      <p>
        Srila Prabhupada explains: 
        <em>"At the beginning of winter, there is a ceremony known as the Odana-sasthi. This ceremony indicates that from that day forward, a winter covering should be given to Lord Jagannatha. That covering is directly purchased from a weaver. According to the arcana-marga, a cloth should first be washed to remove all the starch, and then it can be used to cover the Lord. Pundarika Vidyanidhi saw that the priest neglected to wash the cloth before covering Lord Jagannatha. Since he wanted to find some fault in the devotees, he became indignant."</em> (Cc Madhya 16.78 purport)
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Pastime of Pundarika Vidyanidhi</h3>
      <p>
        The Lord's mercy comes in many forms and the story behind Odana Sasthi is one amongst many to tell. Once, Sri Caitanya Mahaprabhu, accompanied by all His devotees, came to see Lord Jagannatha on Odana Sasthi and was gripped in waves of ecstasy. Musical bands with numerous instruments, mridangas, kartals, conch shells, and bells echoed all around, and the entire town wore a festive look. 
      </p>
      
      <p>
        Srila Pundarika Vidyanidhi and Srila Svarūpa Dāmodara Gosvāmī had a friendly, intimate relationship. They attended the festival of Odana-sasthi in Sri Kshetra Puri Dham. Srila Pundarika Vidyanidhi saw that the Lord was offered new cloth directly purchased from a weaver, which was not washed before being offered. According to proper injunctions to be followed when worshipping the deities, the purchased cloth should first be washed to remove all the starch, which is considered impure.
      </p>
      
      <p>
        Seeing the Lord wearing starched clothes, Pundarika Vidyanidhi became very angry; thus his mind became polluted. He spoke to Srila Svarupa Damodara and criticized the behavior of Lord Jagannatha's servants, asking why they would offer starched cloth to the Lord. Svarūpa Dāmodara Gosvāmī tried to pacify Pundarika Vidyanidhi with gentle words, saying that it was simply how it was done, and surely if the Lord was not pleased, He would have stopped it. Svarupa Damodara remarked that the characteristics of the Supreme Lord are beyond the jurisdiction of worldly etiquette, but Pundarika Vidyanidhi was still not satisfied.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Chastisement and Mercy in a Dream</h3>
      <p>
        That night, Lord Jagannatha and Lord Baladeva came to Pundarika Vidyanidhi in his dream and began to slap him, chastising him for criticizing Their servants and the way they worshipped the Lord. This chastisement went on for a length of forty-eight minutes (one ghati). After Pundarika Vidyanidhi begged for Their forgiveness, the Lords mercifully glanced at Pundarika and went back to Their temple. 
      </p>
      
      <p>
        Upon waking up, Pundarika felt that his face was swollen and bore the imprints of the Lords' hand marks. He understood this to be the pure, intimate mercy of the Lord, and thus upon seeing his swollen face, he laughed with great joy.
      </p>
      
      <p>
        Learning from this incident, we understand that the Lord does not tolerate offenses against His servants, even from an advanced devotee, and that He chastises a devotee who commits such an offense even within the mind. We can also learn that a pure devotee accepts such chastisement from the Lord with great happiness, as a manifestation of the Lord's mercy, His love, and His care. Pundarika Vidyanidhi thanked the Lord for rectifying him and preventing him from committing further offenses, feeling great jubilation within his heart.
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
