      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Glories of Moksada Ekadasi</h3>
      
      <p>
        Yudhishthira Maharaj said, "O Vishnu, master of all, O delight of the three worlds, O Lord of the entire Universe, O creator of the world, O oldest personality, O best of all beings, I offer my most respectful obeisances unto You. O Lord of lords, for the benefit of all living entities, kindly answer some questions that I have. What is the name of the Ekadasi that occurs during the light fortnight of the month of Margashirsha (November-December) and removes all sins? How does one observe it properly, and which Deity is worshipped?"
      </p>
      
      <p>
        Lord Sri Krishna replied, "O dear Yudhishthira, your enquiry is very auspicious in itself and will bring you fame. Just as I previously explained to you about the dearest Utpanna Maha-Dvadashi, so I shall now relate to you regarding this Ekadasi that occurs during the light part of the month of Margashirsha."
      </p>
      
      <p>
        "This Ekadasi is famous as Moksada because it purifies the faithful devotee of all sinful reactions and bestows liberation upon him. The worshipable Deity of this all-auspicious day is Lord Damodara. With full attention, one should worship Him with incense, a ghee lamp, fragrant flowers, and Tulsi manjaris (buds)."
      </p>
      
      <p>
        "O best of saintly kings, please listen as I narrate to you the ancient and auspicious history of this wonderful Ekadasi. Simply by hearing this history, one can attain the merit earned by performing a horse sacrifice. By the influence of this merit, one's forefathers, mothers, sons, and other relatives who have gone to hell can turn around and go to the heavenly kingdom."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Dream of King Vaikhanasa</h3>
      <p>
        "There once was a beautiful city named Champaka-nagar, which was decorated with devoted Vaishnavas. There the best of saintly kings, Maharaj Vaikhanasa, ruled over his subjects as if they were his very own dear sons and daughters. The brahmins in that capital city were all expert in four kinds of Vedic knowledge."
      </p>
      
      <p>
        "The king, while ruling properly, had a dream one night in which his father was seen to be suffering the pangs of hellish torture in one of the hellish planets ruled over by Yamaraja. The king was overwhelmed with compassion for his father and shed tears. The next morning, Maharaj Vaikhanasa described what he had seen to his council of learned brahmins: 'O brahmanas, in a dream last night I saw my father suffering on a hellish planet. He was crying out in anguish, "O son, please deliver me from the torment of this hellish condition!" Now I have no peace in my mind, and even this beautiful kingdom has become unbearable to me. Please tell me what kind of charity, what mode of fasting, what austerity, or what deep meditation must I perform to deliver my father?'"
      </p>
      
      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "The learned brahmins replied, 'O king, in the mountainous forest not far from here is the ashram of the great saint Parvata Muni. Please go to him, for he is tri-kala-jna (he knows the past, present, and future) and can surely guide you.' The king immediately set out to the ashram of the famous sage."
      </blockquote>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Grievous Sin Revealed</h3>
      <p>
        "Approaching the holy ashram, the king beheld Parvata Muni seated among the assembly of sages. The king offered his humble obeisances, prostrating before him. After the king had seated himself, Parvata Muni closed his eyes and meditated on the king's father's past, present, and future. After a few moments, he opened his eyes and said: 'Your father is suffering the results of committing a great sin. In his previous life, he quarreled with his wife and forcibly enjoyed her sexually during her menstrual period. She tried to protest and resist, but he did not stop. It is on account of this grievous sin that your father has fallen into such a hellish condition of suffering.'"
      </p>
      
      <p>
        "King Vaikhanasa asked, 'O greatest among sages, by what process of fasting or charity may I liberate my dear father?'"
      </p>
      
      <p>
        "Parvata Muni replied, 'During the light fortnight of the month of Margashirsha, there occurs an Ekadasi called Moksada. If you observe this sacred Ekadasi strictly, with a full fast, and give directly to your suffering father the merit you thus attain, he will be freed from his pain and instantly liberated.'"
      </p>
      
      <p>
        "Hearing this, Maharaj Vaikhanasa returned to his palace. When the tithi arrived, he and his family observed the Moksada Ekadasi fast with full devotion. He dutifully offered the merit to his father, and as he did, flower petals showered down from the demigods. The king's father was immediately escorted to the celestial regions. As he passed his son, the father said, 'My dear son, all auspiciousness unto you!' At last he reached the heavenly realm, where he began to perform devotional service, returning in due course back to Godhead."
      </p>
      
      <p>
        Lord Sri Krishna concluded, "O son of Pandu, whosoever strictly observes the sacred Moksada Ekadasi achieves full and perfect liberation after death. There is no better fasting day than this Ekadasi, for it is like cintamani, elevating souls from hellish life to the spiritual abode."
      </p>

