      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Glories of Haribodhini (Utthana) Ekadasi</h3>
      <p style="font-size:var(--font-size-sm); color:var(--text-light); font-weight:600; margin-bottom:0;">
        From the Skanda Purana
      </p>
      
      <p>
        Lord Brahma said to Narada Muni, "Dear son, O best of the sages, I shall narrate to you the glories of Haribodhini Ekadasi, which eradicates all kinds of sins and bestows great merit, and ultimately liberation, upon the wise persons who surrender unto the Supreme Lord."
      </p>
      
      <p>
        "O best of the brahmanas, the merits acquired by bathing in the Ganges remain significant only as long as Haribodhini Ekadasi does not come. This Ekadasi is much more purifying than a bath in the ocean, at a place of pilgrimage, or in a lake. This sacred Ekadasi is more powerful in nullifying sin than one thousand Asvamedha sacrifices and one hundred Rajasuya sacrifices."
      </p>
      
      <p>
        Narada Muni inquired, "O father, please describe the relative merits of fasting completely on Ekadasi, eating supper (without grains or beans), or eating once at midday."
      </p>
      
      <p>
        Lord Brahma replied, "If a person eats once at midday on Ekadasi, the sins of his previous birth are erased; if he eats supper, the sins acquired during his previous two births are removed; and if he fasts completely, the sins accumulated during his previous seven births are eradicated. A person whose sins equal Mount Sumeru in volume sees them all reduced to nothing if he simply fasts on Papaharini Ekadasi (Haribodhini). The sins a person has accumulated over a thousand previous births are burned to ashes if he not only fasts but also remains awake throughout Ekadasi night, just as a mountain of cotton can be burned to ashes by a small spark."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Nine Ways to Worship Tulasi Devi</h3>
      <p>
        Lord Brahma highlighted that worshipping Srimati Tulasi Devi in the month of Kartika is highly pleasing to Lord Hari. One who performs these nine services resides in Vaikuntha eternally:
      </p>
      
      <ol style="margin-left:var(--space-lg); line-height:1.8; display:flex; flex-direction:column; gap:6px;">
        <li><strong>Seeing</strong> Tulasi Maharani.</li>
        <li><strong>Touching</strong> her.</li>
        <li><strong>Meditating</strong> on her.</li>
        <li><strong>Narrating</strong> her history and glories.</li>
        <li><strong>Offering obeisances</strong> to her.</li>
        <li><strong>Praying</strong> to her for grace.</li>
        <li><strong>Planting</strong> her.</li>
        <li><strong>Worshipping</strong> her.</li>
        <li><strong>Watering</strong> her.</li>
      </ol>
      
      <p>
        "One who serves Tulasi-devi in these nine ways achieves happiness in the higher world for as many thousands of yugas as there are roots and subroots growing from a mature tulasi plant. When a full-grown tulasi plant produces seeds, many plants grow from those seeds and spread. For as many thousands of kalpas as there are seeds produced in this way, the forefathers of one who serves tulasi will live in the abode of Lord Hari."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Worshipping the Lord with Sacred Flowers</h3>
      <p>
        "One should worship Lord Keshava with camphor, fruits, and aromatic flowers on this day. Worshipping the Lord with <strong>agastya flowers</strong> causes even Lord Indra to offer his respects. Offering <strong>bel leaves</strong> yields immediate liberation. Worshipping with fresh <strong>tulasi leaves</strong> burns to ashes the sins committed for ten thousand births. Offering <strong>kadamba flowers</strong> prevents one from seeing the abode of Yamaraja. Offering <strong>bakula, ashoka, and patali flowers</strong> frees one from misery for as long as the sun and moon exist. Offering <strong>yellow ketaki flowers</strong> frees one from the sins of ten million births."
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "On Haribodhini Ekadasi, a devotee of the Lord should not eat in another's house or eat food cooked by a non-devotee. Philosophical discussion of Scriptures in the month of Kartika pleases Sri Vishnu more than giving elephants and horses in charity. Whoever chants or hears descriptions of Lord Vishnu's qualities, even if but a fourth of a verse, attains the merit of giving away a hundred cows."
      </blockquote>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Dvadashi Parana & Chaturmasya Conclusion</h3>
      <p>
        "On Dvadashi morning, the devotee should rise early, bathe, chant japa, and render loving service. To break the fast, one should first offer prasadam and dakshina to qualified brahmanas. One should worship the spiritual master, offering him clothes, gold, and cows. Since Chaturmasya ends on this day, whatever vow or food item was given up during the four months of Chaturmasya should now be donated to the brahmanas."
      </p>
      
      <p>
        "One who has fasted from ghee should donate milk; one who has fasted from grains should donate rice; one who has slept on the floor should donate a bedstead with a quilt; one who has eaten on a leaf plate should donate a pot of ghee; one who has remained silent should donate a bell. One who follows this process of Chaturmasya receives unlimited merit and goes to the abode of Lord Vasudeva after death."
      </p>

