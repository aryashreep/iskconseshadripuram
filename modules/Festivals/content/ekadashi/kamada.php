      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Glories of Kamada Ekadasi</h3>
      <p style="font-size:var(--font-size-sm); color:var(--text-light); font-weight:600; margin-bottom:0;">
        From the Varaha Purana
      </p>
      
      <p>
        Sri Suta Goswami said, "Oh sages, let me offer my humble and respectful obeisances unto the Supreme Lord Hari, Bhagavan Sri Krishna, the son of Devaki and Vasudeva, by whose mercy I can describe the fast day that removes all kinds of sins. It was to the devoted Yudhishthira that Lord Krishna glorified the twenty-four primary Ekadasis, which destroy sin, and now I shall recount one of those narrations to you. Great-learned sages have selected these twenty-four narrations from the eighteen Puranas, for they are truly sublime."
      </p>
      
      <p>
        Yudhishthira Maharaja said, "Oh Lord Krishna, Oh Vasudeva, please accept my humble obeisances. Please describe to me the Ekadasi that occurs during the light part of the month of Chaitra (March-April). What is its name, and what are its glories?"
      </p>
      
      <p>
        Lord Sri Krishna replied, "Oh Yudhishthira, please listen to Me attentively as I relate the ancient history of this sacred Ekadasi, a history Vasishtha Muni once related to King Dilipa, the great-grandfather of Lord Ramachandra."
      </p>
      
      <p>
        "King Dilipa asked the great sage Vasishtha, 'Oh wise brahmana, I wish to hear about the Ekadasi that comes during the light part of the month of Chaitra. Please describe it to me.'"
      </p>
      
      <p>
        "Vasishtha Muni replied, 'Oh king, your inquiry is glorious. Gladly shall I tell you what you wish to know. The Ekadasi that occurs during the light fortnight of Chaitra is named Kamada Ekadasi. It consumes all sins, as a forest fire consumes a supply of dry firewood. It is very purifying, and it bestows the highest merit upon one who faithfully observes it. Oh king, now hear an ancient history which is so meritorious that it removes all one's sins simply by being heard.'"
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Curse of Lalita</h3>
      <p>
        "Once, long ago, there existed a city-state named Ratnapura, which was decorated with gold and jewels and in which sharp-fanged snakes would enjoy intoxication. King Pundarika was the ruler of this most beautiful kingdom, which numbered many Gandharvas, Kinnaras, and Apsaras among its citizens."
      </p>
      
      <p>
        "Among the Gandharvas were Lalita and his wife Lalita, who was an especially lovely dancer. These two were intensely attracted to each other, and their home was full of great wealth and fine food. Lalita loved her husband dearly, and likewise he constantly thought of her within his heart."
      </p>
      
      <p>
        "Once, at the court of King Pundarika, many Gandharvas were dancing and Lalita was singing alone, without his wife. He could not help thinking about her as he sang, and because of this distraction he lost track of the song's meter and melody. Indeed, Lalita sang the ending of his song improperly, and one of the envious snakes who was in attendance at the king's court complained to the king that Lalita was absorbed in thinking of his wife instead of his sovereign."
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "The king became furious upon hearing this, and his eyes turned crimson with rage. Suddenly he shouted, 'Oh foolish knave, because you were lustfully thinking of a woman instead of reverently thinking of your king as you performed your court duties, I curse you to at once become a cannibal!'"
      </blockquote>

      <p>
        "Lalita immediately became a fearful cannibal, a great man-eating demon whose appearance terrified everyone. His arms were eight miles long, his mouth was as big as a huge cave, his eyes were as awesome as the sun and moon, his nostrils resembled enormous pits in the earth, his neck was a veritable mountain, his hips were four miles wide, and his gigantic body stood a full sixty-four miles high. Thus poor Lalita, the loving Gandharva singer, had to suffer the reaction of his offense against King Pundarika."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Redemption by Sage Shringi</h3>
      <p>
        "Seeing her husband suffering as a horrible cannibal, Lalita became overwhelmed with grief. She thought, 'Now that my dear husband is suffering the effects of the king's curse, what is to be my lot? What should I do? Where should I go?' In this way Lalita grieved day and night. Instead of enjoying life as a Gandharva's wife, she had to wander everywhere in the thick jungle with her monstrous husband, who was wholly engaged in terrible sinful activities."
      </p>
      
      <p>
        "By good fortune, however, Lalita came upon the sage Shringi one day. He was sitting on the peak of the famous Vindhyachala Hill. Approaching him, she immediately offered the ascetic her respectful obeisances. The sage noticed her bowing down before him and said, 'Oh most beautiful one, who are you? Whose daughter are you, and why have you come here? Please tell me everything in truth.'"
      </p>
      
      <p>
        "Lalita replied, 'Oh great sage, I am the daughter of the great Gandharva Viradhanva, and my name is Lalita. I roam the forests and plains with my dear husband, whom King Pundarika has cursed to become a man-eating demon. Oh brahmana, I am greatly aggrieved to see his ferocious form and terribly sinful activities. Oh master, please tell me how I can perform some act of atonement on behalf of my husband to free him from this demonic form.'"
      </p>
      
      <p>
        "The sage replied, 'Oh heavenly maiden, there is an Ekadasi named Kamada that occurs in the light fortnight of the month of Chaitra. It is coming up soon. Whoever fasts on this day has all his desires fulfilled. If you observe this Ekadasi fast according to its rules and regulations and give the merit you thus earn to your husband, he will be freed from the curse at once.' Lalita was overjoyed."
      </p>
      
      <p>
        "Lalita faithfully observed the fast of Kamada Ekadasi according to the instructions of the sage Shringi, and on Dvadashi she appeared before him and the Deity of Lord Vasudeva and said: 'I have faithfully observed the fast of Kamada Ekadasi. By the merit earned through my observance of this fast, let my husband be free from the curse that has turned him into a demoniac cannibal.'"
      </p>
      
      <p>
        "As soon as Lalita finished speaking, her husband, who stood nearby, was at once freed from the king's curse. He immediately regained his original form as the Gandharva Lalita, a handsome heavenly singer adorned with many beautiful ornaments. At last the Gandharva couple boarded a celestial airplane and ascended to heaven."
      </p>
      
      <p>
        Lord Sri Krishna concluded, "Oh Yudhishthira, best of kings, anyone who hears this wonderful narration should certainly observe holy Kamada Ekadasi to the best of his ability, such great merit does it bestow. It can eradicate even the sin of killing a brahmana, and it also nullifies demoniac curses and cleanses the consciousness."
      </p>

