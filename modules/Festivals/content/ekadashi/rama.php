      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Glories of Rama Ekadasi</h3>
      <p style="font-size:var(--font-size-sm); color:var(--text-light); font-weight:600; margin-bottom:0;">
        From the Brahma-Vaivarta Purana
      </p>
      
      <p>
        Yudhishthira Maharaj said, "O Janardana, O protector of all beings, what is the name of the Ekadashi that comes during the dark fortnight (Krishna paksha) of the month of Kartika (October – November)? Please impart this sacred knowledge to me."
      </p>
      
      <p>
        The Supreme Lord, Sri Krishna then spoke as follows: "O lion among kings, please listen as I narrate to you. The Ekadashi that occurs during the dark part of the month of Kartika is called Rama Ekadashi. It is most auspicious, for it at once eradicates the greatest sins and awards one the passage to the spiritual abode. I shall now narrate to you its history and glories."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Strict Fast of King Muchakunda</h3>
      <p>
        "There once lived a famous king of the name Muchakunda, who was friendly with Lord Indra, the king of the heavenly planets, as well as with Yamaraja, Varuna, and Vibhishana, the brother of Ravana. Muchakunda always spoke the truth and constantly rendered devotional service to Me. Because he ruled according to religious principles, there were no disturbances in his kingdom."
      </p>
      
      <p>
        "Muchakunda's daughter was named Chandrabhaga, and the king gave her in marriage to Shobhana, the son of Chandrasena. One day, Shobhana visited his father-in-law's palace on the auspicious Ekadashi day. This visit made Shobhana's wife Chandrabhaga quite anxious, for she knew that her husband was physically very weak and unable to bear the austerity of a day-long fast. She said to him, 'My father is very strict. On Dashami, the day before, he strikes a large kettledrum and announces: Nobody should eat on Ekadashi, the sacred day of Sri Hari!'"
      </p>
      
      <p>
        "When Shobhana heard this, he said to his wife, 'O beautiful one, what am I to do now? Please tell me how I can save my life and obey your father's strictness!'"
      </p>
      
      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "Chandrabhaga spoke, 'My dear husband, in my father's house nobody – not even the elephants, horses, or other animals – eats on Ekadashi. None of the animals are given their ration of grains, straw, or even water! So how can you escape fasting? My beloved husband, if you must eat, then you should leave here at once. Now, decide what you have to do.'"
      </blockquote>

      <p>
        "Prince Shobhana said, 'I have decided to fast on the sacred Ekadashi day. Whatever my fate is, it will surely come to pass.' Deciding thus, Shobhana attempted to fast, but he became unbearably disturbed with hunger and thirst. Eventually the sun set, and the arrival of night made all the Vaishnavas happy. The devotees remained awake all night worshipping the Lord, but Prince Shobhana suffered terribly. Indeed, when the sun rose on the Dvadashi, the Prince was dead."
      </p>
      
      <p>
        "King Muchakunda observed his son-in-law's funeral. He instructed his daughter Chandrabhaga not to join her husband on the funeral pyre. Thus Chandrabhaga, after performing the purificatory processes, continued to live in her father's house."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Mandarachala Kingdom</h3>
      <p>
        "Lord Sri Krishna continued: 'O best of the kings, even though Shobhana died because of observing Rama Ekadashi, the merit that he accrued enabled him, after his death, to become the ruler of a kingdom high on the peak of Mandarachala Mountain. This kingdom was like a city of the demigods; very lustrous, with unlimited jewels set in the walls of its buildings. The pillars were made of rubies, and gold inlaid with diamonds shone everywhere. Shobhana sat upon a throne beneath a pure white canopy, served by Gandharvas and Apsaras. He resembled a second Indra.'"
      </p>
      
      <p>
        "One day, a brahmin named Somasharma, who lived in Muchakunda's kingdom, happened upon Shobhana's kingdom while traveling. Somasharma saw Shobhana in all his resplendent glory and recognized him. Shobhana welcomed him, paid his respectful obeisances, and asked about the welfare of his wife Chandrabhaga and King Muchakunda."
      </p>
      
      <p>
        "Somasharma replied, 'O king, all are well. But I am astonished to find you here! Please tell me how you obtained this city. No one has ever seen such a beautiful city!' King Shobhana replied, 'Because I observed the Rama Ekadashi, I was given this splendid city to rule over. But for all of its grandeur, it is only temporary. You see, I fasted on the Rama Ekadashi without faith, and thus this kingdom is impermanent. Please return to Chandrabhaaga, and tell her what you have seen. Surely, if you tell her this, my city will soon become permanent.'"
      </p>
      
      <p>
        "The brahmin returned and related the entire episode to Chandrabhaga, who was overjoyed. She said, 'O brahmana, is this a dream, or is it actually factual?' Somasharma replied, 'O Princess, I have seen your late husband face to face. But he says his kingdom is unstable and could vanish. Therefore he hopes you can find a way to make it permanent.'"
      </p>
      
      <p>
        "Chandrabhaga said, 'O sage, please take me to that place at once, for I greatly desire to see him! Surely I shall make his kingdom permanent with the merit that I have acquired by fasting on every Ekadashi throughout my life.'"
      </p>
      
      <p>
        "Somasharma led Chandrabhaga to Shobhana's kingdom. Before reaching it, they stopped at the foot of Mt. Mandarachala, at the ashrama of Vamadeva Muni. Upon hearing their story, Vamadeva chanted Vedic hymns and sprinkled holy water upon Chandrabhaaga. By the influence of the Rishi's rites and the merit of her lifelong Ekadashi fasts, her body became transcendental."
      </p>
      
      <p>
        "When Shobhana saw his wife approaching, he was overwhelmed with joy and called out to her. He seated her on his left side, and she said: 'O dearest husband, please listen. Since I was eight years old, I have fasted regularly and with full faith on every Ekadashi. If I transfer to you all the merit I have accumulated, your kingdom will surely become permanent, and its prosperity will grow until the coming of the great inundation!'"
      </p>
      
      <p>
        "O Yudhishthira, in this way Chandrabhaga, beautifully decorated with the finest ornaments and an exquisitely transcendental body, enjoyed peace and happiness with her husband. By the potency of Rama Ekadashi, Shobhana found his kingdom able to fulfill all his desires, like the transcendental Kama-dhenu cow."
      </p>
      
      <p>
        Lord Sri Krishna concluded, "One should not differentiate between the Ekadashis of the light and dark parts of the month. Both award the same high degree of merit, just as black cows and white cows give equally good quality milk. Anyone who simply hears this narration is freed from all kinds of sin and attains the supreme abode of Lord Vishnu."
      </p>

