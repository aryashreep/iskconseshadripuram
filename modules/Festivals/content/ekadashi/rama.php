      <p>
        The 11th day of Krishna Paksha (Moon's waning phase) in the month of Kartik (October – November) is celebrated as Rama Ekadasi, named after Lord Vishnu's consort, Goddess Rama (Lakshmi). The day is also known as Rambha Ekadasi or Kartik Krishna Ekadasi.
