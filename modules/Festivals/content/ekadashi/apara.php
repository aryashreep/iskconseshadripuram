        <li>Rendering service to Lord Shiva at Varanasi (Benares) during Shiva-ratri;</li>
        <li>Offering oblations to one's forefathers at Gaya;</li>
        <li>Bathing in the sacred Gautami River when Jupiter transits Leo (Simha);</li>
        <li>Having darshan of Lord Shiva at Kedaranatha;</li>
        <li>Seeing Lord Badrinath when the Sun transits the sign of Aquarius (Kumbha);</li>
        <li>Bathing at the time of a solar eclipse at Kurukshetra and giving cows, elephants, and gold there in charity.</li>
      </ul>
      
