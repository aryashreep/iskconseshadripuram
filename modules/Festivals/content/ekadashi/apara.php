        From the Brahmanda Purana
      </p>
      
      <p>
        Sri Yudhishthira Maharaj said, "Oh Janardana, what is the name of the Ekadasi that occurs during the dark fortnight (krishna paksha) of the month of Jyeshtha (May-June)? I wish to hear from You the glories of this sacred day of Hari. Please narrate everything to me."
      </p>
      
      <p>
        Lord Sri Krishna said, "Oh king, your inquiry is wonderful because the answer will benefit the whole human society. This Ekadasi is so sublime and meritorious that even the greatest sins can be erased by its purity."
      </p>
      
      <p>
        "Oh great saintly king, the name of this unlimitedly meritorious Ekadasi is Apara Ekadasi. Whoever fasts on this holy day becomes famous all over the universe. Even such sins as killing a brahmana, a cow, or an embryo; blasphemy; or having sex with another man's wife are completely eradicated by observing Apara Ekadasi."
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "Apara Ekadasi is an axe that cuts down the fully matured forest full of trees of sinful deeds, it is a forest fire that burns sins as if they were kindling firewood, it is the sun blazing before one's dark misdeeds, and it is a lion stalking the meek deer of impiety."
      </blockquote>

      <p>
        "Oh king, people who bear false witness are most sinful. A person who falsely or sarcastically glorifies another; one who cheats while weighing something on a scale; one who fails to execute the duties of his varna or ashrama (an unqualified man posing as a brahmin, for example, or a person reciting the Vedas wrongly); one who invents his own scriptures; one who cheats others; one who is a charlatan astrologer, a cheating accountant, or a false Ayurvedic doctor – all these are surely as bad as persons who bear false witness, and they are all destined for hellish punishments. But simply by observing Apara Ekadasi, all such sinners become completely free of their sinful reactions."
      </p>
      
      <p>
        "Warriors who fall from their kshatriya-dharma and flee the battlefield go to a ferocious hell. But, Oh Yudhishthira, even such a fallen kshatriya, if he observes fasting on the Apara Ekadasi, is freed of that great sinful reaction and goes to heaven."
      </p>
      
      <p>
        "That disciple is the greatest sinner who, after receiving a proper spiritual education from his spiritual master, turns around and blasphemes him. Such a so-called disciple suffers unlimitedly. But even he, rascal though he be, if he simply observes Apara Ekadasi, can attain to the spiritual world."
      </p>

