      </p>
      
      <p>
        "One day, as the King was sitting on his throne, Sri Narada Muni suddenly appeared before him from the sky. King Indrasena rose with folded palms, offered his respectful obeisances, and worshipped the sage with sixteen ingredients. Once seated, Narada Muni asked Indrasena about the welfare of his kingdom and his devotion."
      </p>
      
      <p>
        "The King replied: 'O best of sages! Everything is well and auspicious by your kind mercy. Today my life has become successful by your darshana. Please tell me the cause of your blessed visit.'"
      </p>
      
      <p>
        "Narada Muni said: 'O lion-like King! Now hear about a wonderfully fortunate incident. Once I went to the abode of Yamaraja from the abode of Lord Brahma. Yamaraja greeted me respectfully. While there, I saw your greatly pious father in the assembly of Yamaraja. As a result of breaking a vow, your father had to go there. He gave me a message for you. He said: "Indrasena, the king of Mahishmatipuri, is my son. O Lord, please tell him that due to some sinful activities committed in my previous life I am now living in the abode of Yamaraja! Therefore, in order to free me, he should observe the vow of Indira Ekadasi, and transfer its piety to me. Then I will be released." Narada Muni continued: 'This is your father's request. To deliver your father, you should observe this fast.'"
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
