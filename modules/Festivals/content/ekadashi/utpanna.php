        "Thereafter, the demigods along with Lord Vishnu proceeded towards the city of Candravati. A wonderful battle scene arose. Lord Vishnu and the demigods prepared an array of weaponry, and the demons also had a formidable arsenal. When the skirmish commenced, the demigods fought with skill, but were eventually scattered by the demons. Lord Vishnu led a group attack, fighting with the demon Mura."
      </p>
      
      <p>
        "In spite of attacking with various weapons, the Lord was unable to kill Mura. He wrestled with the archfiend for more than ten thousand years! Finally, after a considerable struggle, the Divine Lord defeated Mura and went to Badarikashrama. In Badarikashrama, the Lord rested in a splendid cave called Hemavati."
      </p>

