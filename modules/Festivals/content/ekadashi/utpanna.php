        Arjuna once asked Krishna, "O Janardana, please describe the fruits of fasting, eating at night, or eating at midday only, on the Ekadasi day."
      </p>
      
      <p>
        Lord Krishna replied, "Oh Arjuna, in the early Autumn, there is an Ekadasi that falls during the waning moon of Margashirsha (November). On this auspicious day, a person should take the vow of Ekadasi. In the early morning, he should make a firm sankalpa (vow) to fast. At high noon, he should purify his body, mind, and senses by taking a bath, praying to Mother Earth to destroy his prarabdha karma (reactions from previous births)."
      </p>
      
      <p>
        "Once upon a time, Indra approached the Supreme Personality of Godhead and prayed: 'Oh Lord of the Universe, the demigods have been evicted from their heavenly abodes by the demons. Please protect us. We have fallen to the Earthly stratum and are merged in the ocean of miseries.'"
      </p>
      
      <p>
        "Lord Vishnu asked, 'Who is this great demon who has triumphed over the demigods? What is his name? From whom did he get such power? Have no fear!'"
      </p>
      
      <p>
        "Indra said, 'Oh Lord of all demigods, a mightily ferocious demon named Mura, son of Nadijangha, has attacked us. He lives in the city of Candravati. The Mura demon has invaded the heavenly planets and is now living there. He has dethroned Indra, Agni, Yama, Vayu, Isha, Candrama, Nairita, and Varuna and taken their positions. We demigods could not stop his powerful assault. Oh Lord, please kill this demon.'"
      </p>
      
      <p>
        "Upon hearing this, Lord Vishnu became furiously angry and said, 'Oh king of the demigods, this rascally demon will undoubtedly perish at the hand of My personal might. You should not fear; you can return to the city of Candravati with peace of mind. I will solve this situation post haste!'"
      </p>

