        "The goddess replied: 'If You wish to give a boon unto me, then please give unto me the potency to deliver even the most heinous sins from all who fast on this day. It is my heartfelt desire that even if one simply eats in the evening, that person will receive half the benefit of fasting; and if one eats only at noontime, that person will receive half as much credit again. Please gift me with this auspicious favor.'"
      </p>
      
      <p>
        "The Lord said: 'You are My spiritual energy, and since today is called Ekadasi – your name will be Ekadasi. Anybody who observes fasting on this, your day, will have his sinful reactions alleviated and attain unending spiritual bliss.'"
