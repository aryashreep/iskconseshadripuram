      <p>
        "When Lord Rama was exiled to the forest for fourteen years, He, the goddess Sita, and His divine brother Lakshmana stayed at Panchavati as mendicants. Mother Sita was then kidnapped by the demon Ravana, and Lord Rama seemingly became bewildered by distress. While searching for His beloved consort, the Lord came upon the dying Jatayu, and thereafter killed His enemy Kabandha. The great devotee-vulture Jatayu returned to Vaikuntha after telling Rama how His dear Sita had been abducted by Ravana."
      </p>
      
      <p>
        "Later, Lord Rama and Sugriva, the king of the monkeys, became friends. Together they amassed a grand army of monkeys and bears and sent Hanumanji to Sri Lanka, where he was able to see Janaki (Srimati Sita devi) in an Ashoka grove. He delivered Lord Rama's message and showed the ring proving his authenticity."
      </p>
      
      <p>
        "With the help of Sugriva, Lord Rama proceeded toward Sri Lanka. Upon arriving at the shore of the ocean, He saw that the water was uncommonly deep and hostile. Thus, He said to Lakshmana: 'O son of Sumitra, how can We cross this vast ocean, the unfathomable abode of Varuna? I can see no easy way to cross it, teeming as it is with sharks and other ferocious aquatics.'"
      </p>
      
      <p>
        "Lakshmana replied, 'O best of all beings, the great sage Bakad Albhya (Bakadbhya) lives on an island just four miles from here. He has seen many Brahmas come and go, so aged and wise is he. Let us go to him, take his darshan, and ask him how We can safely reach Our goal.'"
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Instructions of Sage Bakadbhya</h3>
      <p>
        "So Rama and Lakshmana proceeded to the ashrama of Bakad Albhya Muni. Approaching him, the two Lords paid Their respectful obeisances. The sage immediately understood that Sri Rama was actually the Supreme Personality of Godhead, acting like a human being."
      </p>
      
      <p>
        "Rama said, 'O great twice-born brahmina, I have come here to the ocean shore with My monkey and bear warriors to cross the sea, conquer Lanka, and defeat Ravana. Please tell Me how I can cross this vast ocean.'"
      </p>
      
      <p>
        "The sage replied, 'O Lord Sri Rama, I shall tell you of the most exalted of all fasts, observing which You will surely conquer Ravana. Kindly listen with full attention.'"
      </p>

