      <p style="font-size:var(--font-size-sm); color:var(--text-light); font-weight:600; margin-bottom:0;">
        From the Brahmanda Purana
      </p>
      
      <p>
        This Ekadasi is described in the Brahmanda Purana in a conversation between King Mandhata and the great sage Vasishtha.
      </p>
      
      <p>
        King Mandhata once said to Vasishtha Muni, "O great sage, kindly be merciful to me and tell me of a holy fast that will benefit me eternally."
      </p>
      
      <p>
        Vasishtha Muni replied, "O king, kindly listen as I describe the best of all fast days, Amalaki Ekadasi. He who faithfully observes a fast on this Ekadasi obtains enormous wealth, gets free of the effects of all kinds of sins, and attains liberation. Fasting on this Ekadasi is more purifying than donating one thousand cows in charity to a pure brahmana. So please hear me attentively as I tell you the story of a hunter who, though daily engaged in killing innocent animals for his living, achieved liberation by observing a fast on Amalaki Ekadasi."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Kingdom of Vaidisha</h3>
      <p>
        "There was once a kingdom named Vaidisha, where all the brahmanas, kshatriyas, vaishyas, and shudras were equally endowed with Vedic knowledge, great bodily strength, and fine intelligence. Oh lion among kings, the whole kingdom was full of Vedic sounds, not a single person was irreligious, and no one sinned. The ruler of this kingdom was King Pashabinduka, a member of the dynasty of Soma, the moon. He was also known as Chitraratha and was very religious and truthful. It is said that King Chitraratha had the strength of ten thousand elephants."
      </p>
      
      <p>
        "During the reign of Maharaja Chitraratha, not a single person in his kingdom attempted to practice another's dharma (duty); so perfectly engaged in their own dharmas were all the citizens. The people rendered loving devotional service to the Supreme Personality of Godhead, Lord Vishnu, as did the king, who also rendered special service to Lord Shiva. Moreover, twice a month everyone fasted on Ekadasi. In this way, the citizens of Vaidisha lived many long years in great happiness and prosperity."
      </p>
      
      <p>
        "Once, in the month of Phalguna, the holy fast of Amalaki Ekadasi arrived. King Chitraratha realized that this particular fast would bestow especially great benefit, and thus he and all the citizens observed this sacred Ekadasi very strictly, carefully following all the rules."
      </p>
      
      <p>
        "After bathing in the river, the king and all his subjects went to the temple of Lord Vishnu, where an Amalaki tree grew. They offered the tree a pot filled with water, canopy, shoes, gold, diamonds, rubies, pearls, sapphires, and aromatic incense. Then they worshiped Lord Parashurama with these prayers: 'Oh Lord Parashurama, Oh son of Renuka, Oh all-pleasing one, Oh liberator of the worlds, kindly come beneath this holy Amalaki tree and accept our humble obeisances.' Then they prayed to the Amalaki tree: 'Oh Amalaki, Oh offspring of Lord Brahma, you can destroy all kinds of sinful reactions. Please accept our respectful obeisances and these humble gifts.'"
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "After offering these prayers, King Chitraratha and his subjects remained awake throughout the night, praying and worshiping. During this time, a very irreligious hunter who maintained himself by killing animals approached. Tired and hungry, he sat down behind the assembly and witnessed the beautiful worship of Lord Damodara beneath the illuminated Amalaki tree. He listened to the glories of the Lord all night, forced by circumstances to stay awake and fast."
      </blockquote>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Rebirth as King Vasuratha</h3>
      <p>
        "In due time, the hunter died, but the merit he had gained by fasting on Amalaki Ekadasi and hearing the Lord's glories made him eligible to be reborn as a great king named Vasuratha, the son of King Viduratha, ruling over the kingdom of Jayanti."
      </p>
      
      <p>
        "King Vasuratha was strong, handsome, charitable, and a great devotee of Lord Vishnu. One day, while hunting in the jungle, King Vasuratha strayed from the footpath and lost his way. Growing weary, he paused beneath a tree and fell asleep. As he slept, some barbarian tribesmen who were his long-standing enemies came upon him and prepared to kill him with spears, swords, and arrows."
      </p>
      
      <p>
        "But none of their weapons could touch the sleeping king, and the tribesmen grew weak and bewildered. Suddenly, a beautiful woman decorated with ornaments and fiery red eyes appeared from the king's body. With her blazing chakra discus, she quickly killed all the tribal hunters who had tried to slay the sleeping king."
      </p>
      
      <p>
        "When the king awoke, he was astonished to see all his enemies dead. He wondered, 'Who has slain them so violently? Who is my great benefactor?' At that moment he heard a voice from the sky: 'You asked who helped you. Well, who is that person who alone can help anyone in distress? He is none other than Sri Keshava, the Supreme Personality of Godhead, who saves all who take shelter of Him.'"
      </p>
      
      <p>
        "Therefore, King Mandhata," Vasishtha Muni concluded, "anyone who observes this holy Amalaki Ekadasi will undoubtedly attain the supreme abode of Lord Vishnu, so great is the merit earned from this sacred fast."
      </p>

