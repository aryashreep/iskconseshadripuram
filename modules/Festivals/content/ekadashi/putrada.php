      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Glories of Putrada Ekadasi</h3>
      <p style="font-size:var(--font-size-sm); color:var(--text-light); font-weight:600; margin-bottom:0;">
        From the Bhavishya Purana
      </p>
      
      <p>
        The pious and saintly Yudhishthira Maharaj said, "Oh Lord, You have so nicely explained to us the wonderful glories of the Saphala Ekadasi. Now please be merciful to me and explain to me the details of the Ekadasi that occurs in the light fortnight (Shukla or Gaura paksha) of this month. What is its name, and what Deity is to be worshipped? Oh Purushottama, please also tell me how You can be pleased on this day."
      </p>
      
      <p>
        Lord Sri Krishna replied, "Oh saintly king, for the benefit of all humanity I shall now tell you how to observe fasting on the Pausha-shukla Ekadasi. Everyone should observe the rules of the Ekadasi vrata, to the very best of their ability. This injunction also applies to the Ekadasi named Putrada, which destroys all sins and elevates one to the spiritual abode. The Supreme Personality of Godhead Shri Narayana, the original personality, is the worshippable Deity of the Ekadasi, and for His faithful devotees He happily fulfils all desires."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Despair of King Suketuman</h3>
      <p>
        "There was once a kingdom named Bhadravati, which was ruled by King Suketuman. His queen was the famous Shaibya. Because he had no son, he spent a long time in anxiety, thinking: 'If I have no son, who will carry on my dynasty?' In this way the king meditated in a religious attitude, thinking: 'Where should I go? What should I do?' Neither Suketuman nor Queen Shaibya could find happiness anywhere, and they spent their days in deep gloom."
      </p>
      
      <p>
        "Even when they offered tarpana (oblations of water to their forefathers), their mutual misery made them think that it was as undrinkable as boiling water. They thought that they would have no descendants to offer tarpana to them when they died. The king and queen were especially upset to learn that their forefathers were worried that soon there would be no one to offer them tarpana also."
      </p>
      
      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "The king thought to himself: 'It is said that without a son, marriage is wasted. Indeed, for a family man with no son, both his heart and his house remain vacant. Bereft of a son, a man cannot liquidate the debts that he owes his forefathers, the demigods, and other human beings. A son is proof of the pious activities a man performed in his past lifetimes, proving that one has worshipped Lord Vishnu in the past.'"
      </blockquote>

      <p>
        "Suffering constant anxiety, King Suketuman decided to end his misery by committing suicide. But he realized that suicide throws a person into hellish conditions of rebirth, and so he abandoned that idea. At last, he mounted his horse and left for the dense forest alone."
      </p>
      
      <p>
        "In that forest, which was filled with deer, birds, and wild beasts, the king wandered aimlessly. He saw deer, tigers, wild boars, lions, monkeys, snakes, and cow elephants with their calves. Beholding all these animals with their mates and offspring, the king became so sad that he wandered fitfully. Tormented by hunger and thirst at midday, he thought: 'What sinful deed could I have done to suffer like this? I have pleased the demigods with sacrifices and given gifts to worthy brahmins. Why am I suffering?'"
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Assembly of the Vishvadevas</h3>
      <p>
        "Absorbed in these thoughts, the king came upon a beautiful pond filled with lilies, swans, cranes, and lotuses. Nearby were many attractive ashramas, where there resided many saints and sages. When the king saw this, his right arm and right eye began to quiver – an auspicious sign."
      </p>
      
      <p>
        "As the king dismounted and stood before the sages, who were chanting on japa beads, he paid his obeisances. The sages said, 'We are very pleased with you, Oh king. Kindly tell us why you have come here.' The king replied, 'Oh great sages, who are you? What are your names?'"
      </p>
      
      <p>
        "The sages replied, 'Oh king, we are known as the ten Vishvadevas (the sons of Vishva: Vasu, Satya, Kratu, Daksha, Kaala, Kaama, Dhriti, Pururavaa, Maadrava, and Kuru). We have come to this pond to bathe. The month of Magha will soon be here in five days, and today is the famous Putrada Ekadasi. One who desires a son should strictly observe this fast.'"
      </p>
      
      <p>
        "The king said, 'I have tried so hard to have a son. If you are pleased with me, kindly grant the boon of having a good son.'"
      </p>
      
      <p>
        "The sages replied, 'The very meaning of Putrada is "giver of a son." So please observe a complete fast on this Ekadasi day. If you do so, then by our blessing – and the mercy of Lord Keshava – you will obtain a son.'"
      </p>
      
      <p>
        "On the advice of the Vishvadevas, the king observed the fast of Putrada Ekadasi according to the regulations, and on Dvadashi, after breaking his fast, he paid obeisances to them and returned to his palace."
      </p>
      
      <p>
        "Queen Shaibya immediately became pregnant, and exactly as predicted, a beautiful, bright-faced son was born to them. In due course, he became famous as a heroic prince, succeeding his father and ruling the kingdom conscientiously."
      </p>
      
      <p>
        Lord Sri Krishna concluded, "One who wishes to fulfill his desires should strictly observe Putrada Ekadasi. While on this planet, one will surely obtain a son, and after death achieve liberation. Anyone who reads or hears its glories obtains the merit of a horse sacrifice."
      </p>

