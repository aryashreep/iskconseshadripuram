        "O Keshava! O Lord of the universe! O Lord Hari, who have assumed the form of half-man, half-lion! All glories to You! Just as one can easily crush a wasp between one's fingernails, so in the same way the body of the wasp-like demon Hiranyakashipu has been ripped apart by the wonderful pointed nails on Your beautiful lotus hands." &mdash; Sri Dasavatara-stotra
      </blockquote>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Pastime of Prahlada and Hiranyakashipu</h3>
      <p>
        Lord Nrisimhadeva appeared to save Prahlada Maharaja, a five-year-old boy who was being severely tortured by his atheistic father, Hiranyakasipu. Prahlada's only "fault" was that he was Krishna conscious and chanted the holy names. 
      </p>
      
      <p>
        Hiranyakasipu had performed intense austerities and obtained a benediction from Lord Brahma that he could not be killed by any man or animal, in the sky, on water or land, during day or night, by any weapon, or inside or outside any residence. Confident that this made him immortal, he declared himself God and tortured his own son for worshipping Vishnu.
      </p>
      
      <p>
        The Supreme Lord, out of His infinite intelligence, kept all of Brahma's benedictions intact while still finishing the demon:
      </p>
      <ul style="margin-left:var(--space-lg); line-height:1.8; display:flex; flex-direction:column; gap:6px;">
        <li><strong>Neither Man nor Animal:</strong> The Lord appeared as Narasimhadeva (half-man, half-lion).</li>
        <li><strong>Neither Inside nor Outside:</strong> He killed the demon on the threshold (doorway) of the assembly hall.</li>
        <li><strong>Neither in the Sky, Water, nor Land:</strong> He placed the demon on His lap.</li>
        <li><strong>Neither Day nor Night:</strong> He killed him at dusk (the twilight junction of day and night).</li>
        <li><strong>No Weapons:</strong> The Lord ripped him apart using His sharp fingernails.</li>
      </ul>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Nine Processes of Devotional Service</h3>
      <p>
        When Hiranyakasipu asked Prahlada what the best thing he had learned from his teachers was, Prahlada Maharaja replied:
      </p>
      <div style="background:var(--cream); padding:var(--space-md); border-radius:var(--radius-md); font-family:var(--font-subheading); color:var(--dark); margin-bottom:var(--space-sm);">
        <em>"sravanam kirtanam vishnoh smaranam pada-sevanam arcanam vandanam dasyam sakhyam atma-nivedanam"</em> (SB 7.5.23)
      </div>
      <p>
        Prahlada explained that the best education is to cultivate these nine processes of devotional service directed toward Lord Vishnu:
      </p>
      <ol style="margin-left:var(--space-lg); line-height:1.8; display:flex; flex-direction:column; gap:6px;">
        <li><strong>Sravanam:</strong> Hearing about the Lord (like chanting kirtan and reading scripture).</li>
        <li><strong>Kirtanam:</strong> Chanting the glories of the Lord.</li>
        <li><strong>Smaranam:</strong> Remembering the Lord constantly.</li>
        <li><strong>Pada-sevanam:</strong> Serving the lotus feet of the Lord.</li>
        <li><strong>Arcanam:</strong> Offering worship to the deity in the temple.</li>
        <li><strong>Vandanam:</strong> Offering prayers to the Lord.</li>
        <li><strong>Dasyam:</strong> Serving the Lord as His humble servant.</li>
        <li><strong>Sakhyam:</strong> Developing an intimate friendship with the Lord.</li>
        <li><strong>Atma-nivedanam:</strong> Fully surrendering one's mind, body, and soul.</li>
      </ol>

