      <p>
        On Gopashtami day, Nanda Maharaja performed a ceremony for the cows and Krishna (and His friends) in Vrindavan as He reached the *pauganda* age. This was the day Nanda Maharaja sent his children Krishna and Balarama to herd the cows for the first time.
      </p>
      
      <p>
        Initially, this was a celebration meant only for men and boys. However, Srimati Radharani and Her friends also wanted to enjoy the fun. Because of Her resemblance to Subala-sakha (Krishna's close friend), Srimati Radharani put on his dhoti and garments and joined Krishna. The other gopis joined in too, disguised as cowherd boys.
      </p>

