      </blockquote>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">How Gopastami is Celebrated</h3>
      <p>
        On this highly auspicious day, people worship the cows. Early in the morning, devotees bathe the cows, offer them fresh flowers and sweet scents, and decorate them with beautiful clothes. They feed them sweet fodder, perform parikrama (circumambulation) around them, and follow them for some distance as they go out to graze.
      </p>
      
      <p>
        In the evening, when the cows return at the time of dusk (known as *godhuli muhurat*), they are greeted and worshipped once again. Devotees touch the cows' lotus feet and pray for a healthy, prosperous future. Charitable donations are offered to Gaushalas (cow sanctuaries) on this day.
      </p>
      
      <p>
        In the Srimad Bhagavatam, it is stated that Lord Sri Krishna walked barefooted at the age of six, keeping the cows ahead of Him. At that time, His footsteps were not even fully developed. Krishna was delighted when the dust raised by the cows' hooves (*gauraj*) fell upon Him. Krishna walked barefooted in the dust of Vrindavan, leaving the footprints of His lotus feet everywhere. This is the primary reason for the dazzling beauty and spiritual potency of Vrindavan.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Krishna's Intimate Affection for Cows</h3>
      <p>
        It is said that Nanda Maharaja had 9 lakh (900,000) cows, and Lord Sri Krishna was intimately aware of each of their names. All the cows were identified by their specific names. If any cow got separated from the group, Krishna would call her back by playing His flute and calling her name. Due to this immense affection, Krishna is known as Gopal ("protector of cows") and Govinda ("one who gives pleasure to the cows").
      </p>
      
      <p>
        Krishna showed His affection for *gau seva* (cow service) through both His words and His pastimes, establishing Krishna and Balarama as the eternal symbols of cow protection.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Sacred Importance of the Cow</h3>
      <p>
        In Vedic culture, cows are not considered ordinary animals; they are revered as mothers. Shastras state that inside the cow exist thirty-three million demigods and goddesses. Consuming the milk of the indigenous cow (*desi cow*) active-izes the brain tissues, helping devotees understand the subtleties of the shastras. The cow is the ultimate symbol of prosperity and spirituality.
      </p>
      
      <p>
        The objective of Gopastami is to protect cows and provide loving services for their welfare. Places where cow seva is performed are blessed with the infinite grace of Lord Hari. Whether we live in cities or villages, supporting local cow protection and consuming pure dairy products from protected cows contributes to cow seva.
      </p>

