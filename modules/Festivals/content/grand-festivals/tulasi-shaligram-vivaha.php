      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Simple Vivaha Prayog (Home worship guidelines)</h3>
      <p>
        Devotees can easily perform a simplified marriage ceremony at home:
      </p>
      
      <ol style="margin-left:var(--space-lg); line-height:1.8; display:flex; flex-direction:column; gap:6px;">
        <li><strong>Adorning Srimati Tulasi:</strong> Dress her pot in a nice red skirt. Adorn her branches with red bangles, kumkum, and bindhis. Use a fresh or dried turmeric root as a mangala-sutra.</li>
        <li><strong>Kanya Sampradanam (Giving of Bride):</strong> Recite the lineage of the bride and the groom. Tie a red thread (kankabandanam) to the Shaligram's right side and Tulsi's left pot-side.</li>
        <li><strong>Mala Dharanam (Exchange of Garlands):</strong> Place a garland on the Tulsi plant and the Shaligram. Exchange the garlands three times. Tulasi garlands Him first.</li>
        <li><strong>Sindhur Danam:</strong> Mark her leaves and upper trunk with sindhur three times on Lord Shaligram's behalf.</li>
        <li><strong>Vastra Bandhanam (Tying their cloth):</strong> Tie a corner of Shaligram's angavastra to Tulsi's red cloth. Keep them bound for seven nights.</li>
        <li><strong>Saptapadi (Seven Steps):</strong> Take seven symbolic steps around the altar chanting: 
          <em>"OM EKAM ISE VISNUH TVA NAYATU"</em> (First step for strength), 
          <em>"DVE URJE"</em> (Power), 
          <em>"TRINI VRATAYA"</em> (Vows), 
          <em>"CATVARI MAYOBHAVAYA"</em> (Happiness), 
          <em>"PANCA PASUBHYO"</em> (Cows), 
          <em>"SAD RAYAS POSAYA"</em> (Wealth), and 
          <em>"SAPTA SAPRABHYO HOTRABHYO"</em> (Maintaining sacrifices).
        </li>
      </ol>
      
