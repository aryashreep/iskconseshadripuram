      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Legend of Vrinda and Jalandhar</h3>
      <p>
        According to the Padma Purana, Tulasi in her former birth was Vrinda, the daughter of a giant named Nemi and the faithful wife of the demon Jalandhar. Being born in water, Jalandhar claimed sovereignty over the ocean and demanded the fourteen treasures churned out during the Samudra Manthan. He declared war on the demigods and became a cause of great danger. He had a boon that he would be free from death as long as his wife Vrinda remained chaste.
      </p>
      
      <p>
        To defeat the demon, Lord Vishnu assumed the form of Jalandhar and approached Vrinda. In her wrath upon discovering the deceit, Vrinda cursed Lord Vishnu to turn into a black stone (Shalagrama). Lord Vishnu accepted her curse, but in admiration of her unimpeachable chastity, He transformed her into the sacred Tulsi plant and promised to marry her annually. The origin of the ammonite black stones found in the Gandaki River bed is attributed to this legend.
      </p>
      
