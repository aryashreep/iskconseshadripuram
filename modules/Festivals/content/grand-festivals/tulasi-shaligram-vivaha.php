      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Scriptural Origins & Marriage Guidelines</h3>
      <p>
        The marriage of Srimati Tulasi Devi is described by Sri Vasishthaji in ancient times as heard from Lord Brahma, and is detailed in the Narada-pancharatra:
      </p>
      
      <p>
        First, one should plant Tulasi either in the house or the forest, and after three years one may perform her marriage. The auspicious times when one may perform the marriage of Shalagrama and Tulasi are in the month of Kartika, from the Ekadashi (Devutthana) to the Full Moon day.
      </p>
      
      <p>
        A sacrificial arena (yajna-kunda) is established under a canopy (mandapa). After performing shanti-vidhana and installing the sixteen goddesses, one installs an attractive Shalagrama-shila (representing Lakshmi-Narayana). When it is dusk, a golden Deity of Narayana and a silver murti of Tulasi are installed according to one's means. With Vedic mantras, two cloths are tied together, marriage bracelets (kangana) are tied, and the marriage is consecrated. The host performs parikrama of Tulasi four times with the Shalagrama-shila.
      </p>

