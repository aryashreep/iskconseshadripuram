        "If one offers Me with love and devotion a leaf, a flower, fruit or water, I will accept it." &mdash; Bhagavad-gita 9.26
      </blockquote>

      <p>
        Flowers are a special part of our tradition. It is the way we honor special guests, the spiritual master, and the Vaishnavas. Offering flowers is a simple service for Krishna but it's a deep meditation. Whether it is stringing flowers together or pulling them apart, with every flower, our meditation should be an offering out of love and devotion to Krishna.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Preparation for the Abhisheka</h3>
      <p>
        In preparation for the festival, the devotees sit around, plucking out tender petals from different kinds of flowers. There are around five types of chandramallika (chrysanthemum) flowers, three types of marigolds, dopati (impatiens balsamina) and dahlia flowers, and thousands of roses. All the flowers are brought from different parts of India. The plucking proceeds till dusk, as basket after basket gets filled to the brim with fragrant petals. Whoever it may be, young or old, there is that consciousness of humility, generosity, and devotion in them with each flower petal they pluck &mdash; with every single one of them as an offering, and a cry for help to attain the highest perfection of life.
      </p>
      
      <p>
        Srila Prabhupada once explained the festival this way: 
        <em>"Krishna was just a toy in the hands of the Gopis, so one day the Gopis decided that we shall decorate Him. Pusya abhisheka means a ceremony to decorate the deity profusely with flowers, ornaments, cloths. After, there should be lavish feasting and a procession through the streets, so that all the citizens should see how beautiful Krishna appears."</em>
      </p>

