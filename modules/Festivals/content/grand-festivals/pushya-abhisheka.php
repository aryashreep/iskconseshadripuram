      <p>
        In the Skanda Purana 2.2.42, Sri Krishna Pusya abhiseka is described as one of the festivals observed for Lord Jagannath in Puri. The chapter gives an elaborate description of the rituals for observing the festival. In Skanda Purana 2.27.99, the pusya naksatra is also mentioned, wherein it is stated that on a Thursday, Astami tithi, during Sukla-paksa of the month of Vaishakha, in conjunction with the Pusya-naksatra, Lord Jagannath was installed in the original temple in Puri.
