      <p>
        The festival reaches its climax after the Sandhya arati, when it is finally time for the abhisheka. The small Radha Madhava deities (Utsav Murtis) come forward to everyone's delight. Sri Sri Radha Madhava are then showered in flower petals as mantras and chanting of the Holy names go on simultaneously. A beautiful pastime unveils in front of everyone's eyes: the deities get fully covered in a mountain of flower petals showered from all directions. The devotees chant even louder and the kirtan goes even faster. When it finally comes to an end, Their Lordships return to the altar to be dressed in fresh clothes, and everyone goes home with the sweet remembrance of the festival.
      </p>
      
