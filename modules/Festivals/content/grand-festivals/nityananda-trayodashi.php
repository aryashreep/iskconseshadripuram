      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Childhood in Ekachakra</h3>
      <p>
        Nityananda Prabhu was born in Ekachakra, a small village in present-day West Bengal, around the year 1474 to Hadai Ojha and Padmavati. He was born on the thirteenth day of the bright fortnight of the month of Magha. As a child, Nitai loved to enact the pastimes of Sri Krishna or Lord Rama. He did this so authoritatively and with such rapture that the entire village would be immersed in the love of Godhead.
      </p>
      
