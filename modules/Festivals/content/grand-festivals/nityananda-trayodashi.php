        Nitai traveled with Lakshmipati Tirtha for about 20 years, visiting all the holy places in India. He was later initiated by Lakshmipati Tirtha and associated closely with Madhavendra Puri.
      </p>
      
      <p>
        Nityananda Prabhu met Caitanya Mahaprabhu in 1506, when He was 32 years old and the Lord was 20. When He reached Nadia, He hid in the house of Nandan Acharya. Chaitanya Mahaprabhu, aware of the arrival of His eternal associate, dispatched Haridas Thakur and Srivas Pandit to search for Nitai, but they could not find Him. Finally, Chaitanya Mahaprabhu went directly to Nandan Acharya's house, and the ecstasy of their meeting was a sublime experience for all who witnessed it.
      </p>

