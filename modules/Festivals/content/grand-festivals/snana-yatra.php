      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Historical Significance of Snana Yatra</h3>
      <p>
        The origins of the Snana Yatra are deeply rooted in the Skanda Purana, which narrates the story of King Indradyumna, who installed the wooden deities and initiated this sacred bathing festival. This festival commemorates the appearance of Lord Jagannath – the Lord of the Universe. It is said that when King Indradyumna arranged the bathing ceremony for the deities, he set a precedent for the celebration of Snana Yatra, making it an integral part of the Jagannath worship tradition.
      </p>
      
      <p>
        Its association with various legends further enriches the festival's historical significance. One such story involves a profound scholar and staunch devotee of Lord Ganesh who visited Puri during Snana Yatra. Initially reluctant to worship any deity other than his Ishtadevata Ganesh, the scholar was astonished when Lord Jagannath appeared in a huge form resembling Lord Ganesh. This miraculous event led to the tradition of dressing the deities in Hati Vesha (elephant attire) during the Snana Yatra, thus blending Snana Yatra history and divine intervention into a captivating narrative.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Why Snana Yatra is Celebrated</h3>
      <p>
        Snana Yatra is celebrated to honor Lord Jagannath's appearance and perform the sacred ritual of bathing the deities. The festival serves multiple purposes:
      </p>
      
      <ul style="margin-left:var(--space-lg); line-height:1.8; display:flex; flex-direction:column; gap:6px;">
        <li><strong>Spiritual Cleansing:</strong> Devotees believe that witnessing the deities during Snana Yatra absolves them of all sins, making the sight of the Lord being bathed highly auspicious and purifying.</li>
        <li><strong>Preserving Ancient Rituals:</strong> It commemorates the ancient ritual established by King Indradyumna, preserving and continuing the tradition of worshipping Lord Jagannath with grandeur and devotion.</li>
        <li><strong>Renewal:</strong> The sacred bathing festival symbolizes the purification and renewal of the deities, reflecting the cyclical nature of life.</li>
        <li><strong>Cultural Heritage:</strong> Beyond its religious significance, Snana Yatra is a cultural spectacle featuring vibrant processions, traditional music, and elaborate decorations that showcase the rich cultural heritage of Odisha and its devotion to Lord Jagannath.</li>
      </ul>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Rituals and Ceremonies</h3>
      <p>
        The rituals and ceremonies of Snana Yatra are elaborate and meticulously planned, reflecting deep-rooted traditions and spiritual significance:
      </p>

      <p>
        <strong>1. Pahandi Vijay</strong><br>
        On the eve of the Lord Jagannath Snana Yatra, the deities are ceremonially brought out from the temple's inner sanctum in a grand procession called Pahandi Vijay. This procession is a spectacular sight accompanied by music, chanting, and drum beating. The deities, Lord Jagannath, Lord Balabhadra, Goddess Subhadra, and Sudarshana, are taken to the Snana Mandapa (bathing platform). The Snana Mandapa is strategically located northeast of Ananda Bazar and beside the temple's outer wall, known locally as Meghanada Pacheri. It is elevated to a height that allows devotees outside the temple to have a clear view.
      </p>

      <p>
        <strong>2. Fetching of the Holy Water</strong><br>
        The ritual of fetching holy water is an integral part of Snana Yatra. On the morning of the festival, a ceremonial procession is undertaken to draw 108 water pots from the Golden Well (Suna Kua). The well is specially opened only once a year for this purpose. To ensure the sanctity of the water, those involved in drawing it cover their mouths with cloth to avoid contamination even from their breath. The water-filled pots are then preserved in the Bhoga Mandap, where they undergo purification with turmeric (Haladi), whole rice (Java), sandalwood (Chandan), flowers, and perfumes.
      </p>

      <p>
        <strong>3. Jalabhisheka</strong><br>
        The main bathing ritual, known as Jalabhisheka, occurs in the morning. The deities, adorned in silk cloth and smeared with red powder, are bathed with the water from the 108 golden vessels. This sacred bath is performed amidst the chanting of Vedic mantras, ecstatic kirtan, and the blowing of conch shells.
      </p>

      <p>
        <strong>4. Hati Vesha (Elephant Attire)</strong><br>
        In the evening, after the bathing ritual, the deities assume the Hati Vesha, where Lord Jagannath and Lord Balabhadra are dressed as elephants, and Goddess Subhadra is adorned with a lotus flower attire. This unique tradition stems from the legend of the scholar devotee of Lord Ganesh and adds a fascinating dimension to the festival.
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "<strong>5. Anasara (Period of Recuperation)</strong><br>
        Following the Snana Yatra, the deities are believed to fall ill and are kept away from public view for 15 days, known as Anasara. During this time, the Raj Vaidya (the king's physician) treats the deities with specific medicines and offers them only fruits and water mixed with cheese. This period is a time of rest and recovery for the deities after their grand bath."
      </blockquote>

      <p>
        <strong>6. Netrotsava (Festival for the Eyes)</strong><br>
        On the 16th day, after the deities have recovered, they are reintroduced to the public in a festival called Netrotsava, or the Festival for the Eyes. This marks the first appearance of the deities after Snana Yatra, and it is a moment of great joy. The deities, freshly painted and decorated, bless the devotees with their divine presence.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Divine Deities of Snana Yatra</h3>
      <p>
        The Snana Yatra primarily centers around the divine trio—Lord Jagannath, Lord Balabhadra, and Goddess Subhadra. Each deity holds a unique significance:
      </p>
      
      <ul style="margin-left:var(--space-lg); line-height:1.8; display:flex; flex-direction:column; gap:6px;">
        <li><strong>Lord Jagannath:</strong> The Lord of the Universe, character-filled wooden idol with large round eyes and broad smile. He is a manifestation of Lord Krishna, whose glance during Snana Yatra washes away sins.</li>
        <li><strong>Lord Balabhadra:</strong> Elder brother of Lord Jagannath, fair-complexioned and associated with strength. He is bathed and adorned in the Hati Vesha during the festival.</li>
        <li><strong>Goddess Subhadra:</strong> The benevolent sister of Lord Jagannath and Lord Balabhadra, completing the divine trio. She is dressed in a lotus flower attire, representing purity and grace.</li>
        <li><strong>Sudarshana:</strong> The symbolic representation of Lord Vishnu's divine discus, acting as a protector to dispel evil and bring righteousness.</li>
      </ul>

