      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Why is it known as the Festival of Punishment?</h3>
      <p>
        Srila Raghunatha dasa Gosvami, a personal associate of Sri Chaitanya Mahaprabhu, displayed a spirit of complete renunciation and detachment from the material world at a very early age. He wanted to leave home to join Sri Chaitanya Mahaprabhu's mission. However, Mahaprabhu instructed him to remain at home and assured him that Krishna would deliver him soon.
      </p>
      
      <p>
        Two years later, when Lord Nityananda came to Panihati, Raghunatha dasa took permission from his father and went to meet Him. He saw Nityananda Prabhu sitting on a rock under a banyan tree on the banks of the Ganges, surrounded by many devotees. Raghunatha dasa was hesitant to approach and paid his obeisances from a distance. 
      </p>
      
      <p>
        Upon noticing him, Lord Nityananda called out: 
        <em>"Raghunatha dasa! You are hiding like a thief. Now I have caught hold of you. Come here. I shall punish you today!"</em> 
        Lord Nityananda forcibly caught him, placed His lotus feet on Raghunatha dasa's head, and ordered him to celebrate a big festival, serving yogurt and chipped rice (chida-dahi) to all the devotees.
      </p>

      <blockquote style="border-left:4px solid var(--maroon); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "dadhi, ciḍā bhakṣaṇa karāha mora gaṇe<br>
        śuni ānandita haila raghunātha mane"<br><br>
        "Make a festival and feed all My associates yogurt and chipped rice. Hearing this, Raghunātha dāsa was greatly pleased." &mdash; Sri Caitanya Caritamrta Madhya Lila 16.78
      </blockquote>

      <p>
        Raghunatha dasa immediately purchased chipped rice, milk, yogurt, sweetmeats, bananas, sugar, and other eatables. The chipped rice was soaked in milk. Half was mixed with yogurt, sugar, and bananas, and the remaining half was mixed with condensed milk, clarified butter, and camphor. All the devotees received two earthen pots, one of each preparation.
      </p>

