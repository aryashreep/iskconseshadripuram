      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Origin of Radha-kund</h3>
      <p>
        The pastime begins after Lord Krishna killed the bull-demon Aristasura. When Krishna approached Srimati Radharani to engage in loving pastimes, She rebuked Him, stating that He was impure because He had committed the sin of killing a bull &mdash; the symbol of religion. She suggested that to counteract this sin, He must bathe in all the sacred rivers of the universe.
      </p>
      
      <p>
        Krishna, eager to please Srimati Radharani, shoved His lotus foot into the ground, creating a large cavity, and called all the sacred rivers in person to enter into it. They appeared before Him, offering prayers, and entered the cavity to form Syama-kunda (the bathing place of Syama).
      </p>
      
      <p>
        After bathing, Krishna teased Radharani, stating that He had become pure, but the gopis had become impure by siding with a demon (even though he appeared as a bull). Radharani and Her gopi friends then broke their bangles and dug a hole in the ground with the broken pieces. Since there was no water in it, Krishna laughed. The gopis then formed a line to the Manasi Ganga, passing water from one to another to fill the hole. At that time, all the personified sacred rivers emerged from Syama-kunda, paid obeisances to Radharani, and requested Her permission to enter Her kunda. Upon receiving Radharani's blessing, they entered, forming Radha-kunda.
      </p>

