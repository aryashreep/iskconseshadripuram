      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl);">Srila Gopal Bhatta Goswami & Lord Caitanya</h3>
      <p>
        The all-merciful Lord Sri Gaura Hari, as He traveled from village to village throughout the south of India, continued to distribute love of Godhead wherever He went. In 1511 A.D., Sri Gaurasundar came to the ancient holy place of Sri Rangam on an island in the middle of the Kaveri river.
      </p>
      <p>
        Sri Vyenkata Bhatta, a priest of the temple, invited Mahaprabhu to stay at his house during the four months of the rainy season (Chaturmasya). Vyenkata Bhatta had a son named Gopal, who was then just a child. When the boy came to offer his obeisances, Mahaprabhu picked him up and affectionately seated him in His lap. He would call Gopal after He had finished His meals and offer His remnants to the boy, preparing him for the position of Acarya.
      </p>
      <p>
        Being pleased with Gopala Bhatta Gosvamis sincere service, Sri Caitanya Mahaprabhu initiated him and ordered him to move to Vrindavana after the disappearance of his parents, to perform bhajan, and write.
      </p>

      <blockquote style="border-left:4px solid var(--primary); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "Sri Sri Radha-ramanaji is the only one of the original Deities of Vrindavana who never left Vrindavana to go to Jaipur."
      </blockquote>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Arrival in Vrindavan and Shaligrama Shilas</h3>
      <p>
        At the age of thirty, Gopala Bhatta Gosvami arrived in Vrindavana. Rupa and Sanatana Gosvamis treated him with the same care and affection they would their own brother, immediately becoming life-long companions. 
      </p>
      <p>
        Sri Caitanya Mahaprabhu, hearing of Gopal Bhatta's arrival, sent him His personal wooden sitting plank (piri) along with His undergarments and an upper cloth (chaddar). Gopal Bhatta sat on this wooden plank during worship, and it is still worshiped today at the Radha-Ramana Mandir.
      </p>
      <p>
        After Mahaprabhu's disappearance, Gopala Bhatta Gosvami felt intense separation. The Lord instructed him in a dream: <em>"If you want my darshan, make a trip to Nepal."</em> In Nepal, he bathed in the Kali-Gandaki River, where twelve Shaligrama Shilas entered his water pot. Gopal Bhatta brought these shilas back to Vrindavana and worshiped them daily.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Self-Manifestation of Radha-Ramana Devji</h3>
      <p>
        One day, a wealthy merchant came to Vrindavana and offered Gopala Bhatta valuable clothing and ornaments for his Shaligrams. Because Shaligrams are round, Gopal Bhatta could not use these decorations. He placed the ornaments before the shilas and prayed in transcendental lamentation:
      </p>
      <p style="font-style:italic; background:var(--light); padding:var(--space-md); border-radius:var(--radius-sm); color:var(--text-dark); border-left:3px solid var(--maroon);">
        "Oh my Lord, you are very merciful and always fulfill the desires of your devotees. I wish to serve you in your form, having arms and legs and a blissful smiling face, with lotus eyes. If I had a Deity then I would be able to decorate Him so nicely with these clothes and ornaments."
      </p>
      <p>
        The next morning, after bathing in the Yamuna, Gopala Bhatta returned to render worship. When he uncovered the shilas under the wicker basket, he was astounded. Amongst the shilas was a Deity of Krishna playing the flute. The Damodara shila had manifested as the beautiful three-fold bending form of <strong>tri-bhangananda-krishna</strong>. 
      </p>
      <p>
        Gazing on the Lord's form, Rupa, Sanatana, and other devotees were filled with ecstasy. They named the Deity <strong>Sri Radha-raman Deva</strong>. This miracle happened on the day after Narasimha Caturdasi, on the full moon day of Vaisakha in 1542 A.D.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Deity Features and Standard of Worship</h3>
      <p>
        Sri Sri Radha-raman-ji is still being worshiped in Vrindavana near Nidhiban kunj in the temple compound. The Deity has extremely intricate features, including fingernails and teeth. On the back of His body, parts of the original Shalagram shila from which He manifested can still be seen.
      </p>
      <p>
        The Deity is called Radha-raman ("one who brings pleasure to Srimati Radharani") although physically there is no Deity of Radha on the altar. To Ramanji's left is a gold place setting representing Srimati Radhika. In this way, the pujaris worship Sri Radha and Ramanji together.
      </p>
      <p>
        The descendants of Gopala Bhatta's brother, Damodara dasa (the Radha-Raman Gosais), maintain and serve the Deity to this day with the highest standard of Deity worship in Vraja.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Gopala Bhatta's Legacy & Literature</h3>
      <p>
        Gopala Bhatta was the initiating guru of Srinivasa Acarya Prabhu. He was very learned, trained in Pancharatra, and wrote many books including <em>Sat-sandarbha karika</em>, <em>Sri Krsna-vallabha</em>, and <em>Sat kriya sara dipika</em> (Vaishnava samskara viddhi). From his initial compile, Srila Sanatana Gosvami published the famous Vaishnava smriti titled <em>Hari Bhakti Vilas</em>.
      </p>
      <p>
        Out of great humility, Gopala Bhatta Gosvami requested Srila Krsnadasa Kaviraja not to mention his name in the classic work <em>Sri Caitanya Charitamrita</em>. In nitya-vraja lila, he serves as Guna Manjari.
      </p>

