      <p>
        His Divine Grace <strong>Śrīla Saccidānanda Bhakti Vinod Ṭhākur</strong> was born on September 2nd, 1838, in Bir Nagar near Navadwip, in the state of West Bengal in India. He was given the name Kedarnath Dutta by his parents, Bir Chandra Dutta and Jagan Mohini Devi Dasi.
      </p>
      
      <p>
        His appearance took place at a critical and historical juncture for the Gaudiya Vaishnavas, when the pure teachings of Sri Caitanya Mahaprabhu and the Goswamis were almost lost due to corruption and pseudo-sects. The original devotional scriptures were so scarce that it took Bhaktivinoda Thakura eight years to locate a single copy of the <em>Caitanya Caritamrta</em>. He single-handedly re-established the teachings of Lord Caitanya, re-discovered the birth site of the Lord (Yogapitha) in Mayapur, and brought pure Vaishnavism back to the forefront of society.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">The Pioneer and Grandfather of the Movement</h3>
