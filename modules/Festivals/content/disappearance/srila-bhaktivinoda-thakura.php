        Srila Bhaktivinoda Thakura is revered as the pioneer and the grandfather of the modern Hare Krishna Movement. In 1896, he initiated worldwide preaching by sending his English book, <em>The Life and Precepts of Sri Caitanya</em>, to learned institutions and libraries across the globe.
      </p>
      <p>
        He prayed to the Lord for a "ray of Vishnu" to assist him in spreading the teachings of Sri Caitanya Mahaprabhu. In response to his prayers, his illustrious son, Srila Bhaktisiddhanta Sarasvati Thakura (the spiritual master of ISKCON's Founder-Acharya, His Divine Grace A.C. Bhaktivedanta Swami Prabhupada), was born. Srila Bhaktivinoda Thakura also famously predicted: 
      </p>

