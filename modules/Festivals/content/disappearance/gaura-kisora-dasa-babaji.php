        <p style="font-size:12px; color:var(--text-light); margin-top:8px; font-style:italic;">Shrila Gaura Kishora Dasa Babaji Maharaj absorbed in deep meditation</p>
      </div>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Spiritual Moods & Association with Bhaktivinoda</h3>
      <p>
        During his stay at Navadvipa, Gaura Kisora underwent various transformations of spiritual moods. Sometimes he danced on the bank of the Ganges chanting, <em>"Gaura, Gaura"</em>, while at other times he would lie on the ground in an unconscious state of ecstasy. He joyfully moved throughout the groves located on the bank of the Ganges, considering them sites of the divine sports of Sri Sri Radha Govinda.
      </p>
      <p>
        Babaji Maharaja would often visit Bhaktivinoda Thakura. During his last days in retirement, absorbed in Krsna katha, he would hear Srimad-Bhagavatam at Bhaktivinoda's house and they would discuss together. Though Babaji Maharaja could neither read nor write, he was regarded as vastly learned and self-realized. His only possessions were the Tulasi beads around his neck and the japa mala he kept in his hand. Sometimes he wore no Tulasi mala on his neck and would chant on knotted cloth as beads—such was his renunciation.
      </p>
      <p>
        Sometimes he would live under an old broken overturned boat, and other times he would scatter fish bones around a place he occasionally used as his bhajan kutir to make materialists think he was a fish-eater, and thus they would not disturb him. But by his pure bhajan, he purified the three worlds.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Initiation of Siddhanta Saraswati</h3>
      <p>
        In 1898, Bhaktivinoda's son, Siddhanta Saraswati (later Srila Bhaktisiddhanta Sarasvati Thakura), came in contact with the person who was to be his spiritual master. At that first meeting, the crest jewel of avadhutas was wearing a tigerskin hat and carrying a basket with puja paraphernalia inside. Bhaktivinoda later told his son, <em>"You must take initiation from Babaji Maharaja, and don't return to this house if you don't."</em>
      </p>
      <p>
        Gaura Kisora dasa Babaji used to walk across a bridge at the same time daily, so on one occasion, Siddhanta Saraswati blocked his path and in all humility said, <em>"If you don't give me initiation then I will finish my life by throwing this useless body off this bridge."</em>
      </p>
      <p>
        Gaura Kisora dasa Babaji Maharaja didn't want any disciples—he himself had taken siksa from Bhaktivinoda, and this was Bhaktivinoda's dear son! However, seeing Siddhanta Saraswati's absolute determination, Babaji Maharaja gave him initiation in 1900 under the name <strong>Varsabhanavi-devi-dayita dasa</strong>.
      </p>

      <blockquote style="border-left:4px solid var(--accent); padding-left:var(--space-md); font-style:italic; color:var(--dark); margin:var(--space-md) 0;">
        "I will never accept this service. Better I throw myself in the Saraswati (Jalanghi) and drown."
      </blockquote>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Refusal of Material Association</h3>
      <p>
        In 1908, Gaura Kisora dasa Babaji Maharaja lost his external eyesight. He then stopped traveling and sat in his bhajan kutir, internally absorbed in Krsna's pastimes. He could be heard in a very deep voice calling the names of the gopis of Vrndavana.
      </p>
      <p>
        Once, Siddhanta Saraswati offered to take Babaji Maharaja to Calcutta to see an eye specialist, but he refused saying, <em>"Never! I will never go to the material world."</em> His disciple protested, <em>"But in Calcutta I could serve you nicely. You wouldn't have to undergo any inconvenience."</em> But Babaji Maharaja was adamant declaring, <em>"I will never accept this service. Better I throw myself in the Saraswati and drown."</em>
      </p>
      <p>
        One day Srila Gaura Kisora dasa Babaji Maharaja wrapped his body and two feet with a cloth. Sitting covered like this he said:
      </p>
      <p style="font-style:italic; background:var(--light); padding:var(--space-md); border-radius:var(--radius-sm); color:var(--text-dark);">
        "Many persons, after being informed by others, come here to collect the dust from my feet. I tell them I am not a Vaisnava. If you go to the vicinity where there are Vaisnavas with their feet pretentiously decorated and extended to touch, you can have unlimited dust. How can I bless you? I don't have barfi or sandesh or rasagulla, or even sweet words. How can I bless you? Nowadays people are interested in spiritual masters who can give these things—wealth, good wife, sweet words—these cheating things are now taken as anukula (favorable)."
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Bhajan Anywhere: The Toilet Room Story</h3>
      <p>
        Giribabu and his wife once ardently requested Gaurakisora to stay in their house at Navadvipa. Gaurakisora was moved by their sincere devotion and finally agreed to oblige them on the condition that he would live only in their toilet room where he would perform Hari bhajan.
      </p>
      <p>
        Giribabu tried to persuade him to change his mind but Gaurakisora remained firm. Giribabu reluctantly arranged to have the toilet thoroughly cleaned, and Gaurakisora used it for Haribhajan. A realized soul can practice Haribhajan anywhere in an unconcerned manner, and wherever he resides, that place becomes Vaikuntha.
      </p>

      <h3 style="color:var(--primary); font-family:var(--font-heading); font-size:var(--font-size-xl); margin-top:var(--space-lg);">Departure</h3>
      <p>
        On June 22, 1914, Bhaktivinoda Thakura passed from this world. Shortly after, on November 17, 1915, on the auspicious day of Utthana Ekadasi, Srila Gaura Kisora dasa Babaji Maharaja also left this world, entering into his eternal samadhi.
      </p>

